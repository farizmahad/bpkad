											tidak mempunyai batas- batas periode untuk memberikan manfaat kepada
											entitas. Oleh karena itu, atas aset tak berwujud yang mempunyai umur
											manfaat yang tak terbatas, harus dilakukan reviu secara berkala untuk
											melihat kemampuan aset tersebut dalam memberikan manfaat.
										</p>
									</li>
									<li>
										<p>
									</li>
									<li>
										<div id="step6" class="section-block"><strong
												class="block-title">PENGAKUAN</strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"> </strong>
										</div>
										</p>
										<p>
											6. Aset tak berwujud diakui pada saat:
										</p>
										<p>
											a. Kemungkinan besar diperkirakan manfaat ekonomi di masa datang yang
											diharapkan atau jasa potensial yang diakibatkan dari aset tak berwujud
											tersebut akan mengalir kepada/dinikmati oleh entitas; dan
										</p>
										<p>
											b. Biaya perolehan atau nilai wajarnya dapat diukur dengan andal.
										</p>
										<p>
											Suatu entitas harus menilai kemungkinan manfaat ekonomi di masa datang yang
											diharapkan atau jasa potensial dengan menggunakan dukungan asumsi logis
											yang mewakili estimasi terbaik dari manajemen tentang kondisi ekonomi yang
											akan diperoleh selama umur ekonomis dari aset tak berwujud. Entitas
											menggunakan pertimbangan untuk menilai derajat kepastian aliran manfaat
											ekonomi di masa datang sebagai akibat dari penggunaan aset tak berwujud
											dengan basis bukti-bukti yang tersedia pada saat pengakuan awal, dengan
											lebih menekankan pada bukti-bukti eksternal.
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"> Pengeluaran
												Setelah Perolehan</strong></div>
										</p>
										<p>
											7. Pada kebanyakan kasus, sifat alamiah aset tak berwujud adalah tidak
											adanya penambahan nilai aset tak berwujud dan tidak diperlukannya
											penggantian dari bagian aset tak berwujud dimaksud setelah perolehan awal.
											Oleh karena itu, kebanyakan pengeluaran setelah perolehan aset tak berwujud
											mungkin dimaksudkan untuk memelihara manfaat ekonomi di masa datang atau
											jasa potensial yang terkandung dalam aset tak berwujud dimaksud dan
											pengeluaran tersebut bukan merupakan upaya untuk memenuhi definisi dan
											kriteria pengakuan aset tak berwujud. Dengan kata lain, seringkali sulit
											untuk mengatribusikan secara langsung pengeluaran setelah perolehan
											terhadap suatu aset tak berwujud tertentu sehingga diperlakukan sebagai
											beban operasional suatu entitas.
										</p>
										<p>
											8. Namun demikian, apabila memang terdapat pengeluaran setelah perolehan
											yang dapat diatribusikan langsung terhadap aset tak berwujud tertentu, maka
											pengeluaran tersebut dapat dikapitalisasi ke dalam nilai aset tak berwujud
											dimaksud.
										</p>
										<p>
											9. Kapitalisasi terhadap pengeluaran setelah perolehan terhadap <em>software
											</em>komputer harus memenuhi salah satu kriteria ini:
										</p>
										<p>
											a. Meningkatkan fungsi <em>software;</em>
										</p>
										<p>
											b. Meningkatkan efisiensi <em>software.</em>
										</p>
										<p>
											10. Terdapat kemungkinan pengembangan suatu aset tak berwujud yang
											diperoleh secara internal yang jangka waktu penyelesaiannya melebihi satu
											tahun anggaran atau pelaksanaan pengembangannya melewati tanggal pelaporan.
											Dalam hal terjadi seperti ini, maka atas pengeluaran yang telah terjadi
											dalam rangka pengembangan tersebut sampai dengan tanggal pelaporan harus
											diakui sebagai aset tak berwujud dalam pengerjaan (Intangible Asset-Work In
											Progress), dan setelah pekerjaan selesai kemudian akan direklasifikasi
											menjadi Aset Tidak Berwujud yang bersangkutan.
										</p>
									</li>
