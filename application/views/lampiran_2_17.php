											11. Aset tak berwujud disajikan sebesar:
										</p>
										<p>
											a. Harga beli, termasuk biaya import, pajak-pajak, setelah dikurangi dengan
											potongan harga dan rabat serta setiap biaya yang dapat diatribusikan secara
											langsung dalam membawa aset tersebut ke kondisi yang membuat aset tersebut
											dapat bekerja untuk penggunaan yang dimaksudkan untuk aset tak berwujud
											yang diperoleh dengan pembelian, kerjasama atau pengembangan internal;
										</p>
										<p>
											b. Nilai wajar, apabila diperoleh dari pertukaran atau donasi/hibah.
										</p>
										<p>
											c. ATB yang berasal dari aset bersejarah (<em>heritage assets</em>) tidak
											diharuskan untuk disajikan di neraca namun aset tersebut harus diungkapkan
											dalam Catatan atas Laporan Keuangan. Namun apabila ATB bersejarah tersebut
											didaftarkan untuk memperoleh hak paten maka hak patennya dicatat di neraca
											sebesar nilai pendaftarannya.
										</p>
