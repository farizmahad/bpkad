											20. Dalam satu periode pelaporan, dikarenakan alasan dan kondisi tertentu,
											entitas pelaporan mungkin melakukan perubahan anggaran dengan persetujuan
											DPRD. Agar pembaca laporan keuangan dapat mengikuti kondisi dan
											perkembangan anggaran, penjelasan atas perubahan-perubahan yang ada, yang
											disetujui oleh DPRD, dibandingkan dengan anggaran pertama kali disahkan
											akan membantu pembaca dalam memahami kondisi anggaran dan keuangan entitas
											akuntansi/pelaporan.
										</p>
										<p>
											21. Ikhtisar pencapaian target keuangan merupakan perbandingan secara garis
											besar antara target sebagaimana yang tertuang dalam APBD dengan
											realisasinya. Ikhtisar ini disajikan untuk memperoleh gambaran umum tentang
											kinerja keuangan pemerintah dalam merealisasikan potensi pendapatan-LRA dan
											alokasi belanja yang telah ditetapkan dalam APBD.
										</p>
										<p>
											22. Ikhtisar ini disajikan baik untuk pendapatan-LRA, belanja, maupun
											pembiayaan dengan struktur sebagai berikut :
										</p>
										<p>
											a) nilai target total;
										</p>
										<p>
											b) nilai realisasi total;
										</p>
										<p>
											c) prosentase perbandingan antara target dan realisasi; dan
										</p>
										<p>
											d) alasan utama terjadinya perbedaan antara target dan realisasi.
										</p>
										<p>
											23. Untuk membantu pembaca laporan keuangan, manajemen entitas
											akuntansi/pelaporan mungkin merasa perlu untuk memberikan informasi
											keuangan lainnya yang dianggap perlu untuk diketahui pembaca, misalnya
											kewajiban yang memerlukan ketersediaan dana dalam anggaran periode
											mendatang.
