										<p>
											26. Sesuai dengan Kerangka Konseptual Kebijakan Akuntansi Pemerintah
											Daerah, asumsi dasar dalam pelaporan keuangan di lingkungan pemerintah
											adalah anggapan yang diterima sebagai suatu kebenaran tanpa perlu
											dibuktikan agar Kebijakan Akuntansi dapat diterapkan, yang terdiri dari:
										</p>
										<p>
											(a) Asumsi kemandirian entitas;
										</p>
										<p>
											(b) Asumsi kesinambungan entitas; dan
										</p>
										<p>
											(c) Asumsi keterukuran dalam satuan uang (<em>monetary measurement</em>).
										</p>
										<p>
											27. Asumsi kemandirian entitas berarti bahwa setiap unit organisasi
											dianggap sebagai unit yang mandiri dan mempunyai kewajiban untuk menyajikan
											laporan keuangan sehingga tidak terjadi kekacauan antar unit instansi
											pemerintah daerah dalam pelaporan keuangan. Salah satu indikasi
											terpenuhinya asumsi ini adalah adanya kewenangan entitas untuk menyusun
											anggaran dan melaksanakannya dengan tanggung jawab penuh. Entitas
											bertanggung jawab atas pengelolaan aset dan sumber daya di luar neraca
											untuk kepentingan yurisdiksi tugas pokoknya, termasuk atas kehilangan atau
											kerusakan aset dan sumber daya dimaksud, utang-piutang yang terjadi akibat
											keputusan entitas, serta terlaksana tidaknya program yang telah ditetapkan.
										</p>
										<p>
											28. Laporan keuangan disusun dengan asumsi bahwa entitas
											akuntansi/pelaporan akan berlanjut keberadaannya. Dengan demikian,
											pemerintah daerah diasumsikan tidak bermaksud melakukan likuidasi atas
											entitas akuntansi/pelaporan dalam jangka pendek.
										</p>
										<p>
											29. Laporan keuangan entitas pelaporan/akuntansi harus menyajikan setiap
											kegiatan yang diasumsikan dapat dinilai dengan satuan uang. Hal ini
											diperlukan agar memungkinkan dilakukannya analisis dan pengukuran dalam
											akuntansi.
										</p>
										<p>
											30. Setiap entitas perlu mempertimbangkan jenis kegiatan-kegiatan dan
											kebijakan-kebijakan yang perlu diungkapkan dalam Catatan atas Laporan
											Keuangan. Sebagai contoh, pengungkapan informasi untuk pengakuan pendapatan
											pajak, retribusi dan bentuk-bentuk lainnya dari iuran wajib, penjabaran
											mata uang asing, dan perlakuan akuntansi terhadap selisih kurs.
										</p>
										<p>
											31. Laporan keuangan seharusnya menunjukkan hubungan angka-angka dengan
											periode sebelumnya. Jika perubahan kebijakan akuntansi berpengaruh
											material, perubahan kebijakan dan dampak perubahan secara kuantitatif harus
											diungkapkan.
