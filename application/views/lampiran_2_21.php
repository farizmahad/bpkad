								<input type="text" class="live-search-box" placeholder="cari disini" />
								<ul class="live-search-list">

										<p>LAMPIRAN II.21  :	PERATURAN WALIKOTA CIMAHI</p>
										<p>NOMOR	   	:	5 Tahun 2018</p>
										<p>TANGGAL  	:		14 Februari 2018</p>
										<p>TENTANG  	:	KEBIJAKAN AKUNTANSI BERBASIS AKRUAL PEMERINTAH DAERAH KOTA CIMAHI</p>
									
									<p align="center">
										<li>
											<div id="step" class="section-block"><strong class="block-title">KEBIJAKAN AKUNTANSI</strong></div>
									</p>
									<p>
										Basis akuntansi yang digunakan dalam penyusunan Laporan Keuangan Pemerintah
										Daerah (LKPD) adalah basis akrual. Penyajian pendapatan-LO, beban, aset,
										kewajiban, dan ekuitas dalam LKPD diakui berdasarkan basis akrual, yaitu
										pada saat diperolehnya hak atas aset atau pendapatan dan timbulnya
										kewajiban atau beban tanpa memperhatikan saat kas atau setara kas diterima
										atau dikeluarkan dari KUD.
									</p>
									<p>
										Laporan Realisasi Anggaran disusun menggunakan basis kas sesuai dengan
										basis dalam penyusunan dan pelaksanaan anggaran. Basis kas adalah basis
										akuntansi yang mengakui pengaruh transaksi dan peristiwa lainnya pada saat
										kas atau setara kas diterima pada Kas Umum Daerah (KUD) atau dikeluarkan
										dari KUD.
									</p>
									<p>
										Penyusunan dan penyajian LKPD Tahun 200X telah mengacu pada Standar
										Akuntansi Pemerintahan (SAP) yang telah ditetapkan dengan Peraturan
										Pemerintah Nomor 71 Tahun 2010 tentang Standar Akuntansi Pemerintahan.
										Dengan demikian, dalam penyusunan LKPD telah diterapkan kaidah-kaidah
										pengelolaan keuangan yang sehat di lingkungan pemerintah daerah.
									</p>
									<p>
										Prinsip-prinsip akuntansi yang digunakan dalam penyusunan LKPD adalah:
									</p>
									<p>
										</li>
										<li>
											<div id="step1" class="section-block"><strong class="block-title">1) Pendapatan
												</strong></div>
									</p>
									<p>
										Pendapatan-LO diakui pada saat timbulnya hak atas pendapatan tersebut atau
										ada aliran masuk sumber daya ekonomi. Pendapatan-LRA adalah semua
										penerimaan KUD yang menambah ekuitas dalam periode tahun yang bersangkutan
										yang menjadi hak pemerintah daerah dan tidak perlu dibayar kembali oleh
										pemerintah daerah. Pendapatan-LRA diakui pada saat kas diterima pada KUD.
										Akuntansi pendapatan dilaksanakan berdasarkan azas bruto, yaitu dengan
										membukukan penerimaan bruto, dan tidak mencatat jumlah netonya (setelah
										dikompensasikan dengan pengeluaran). Pendapatan disajikan sesuai dengan
										jenis pendapatan.
									</p>
									<p>
										</li>
										<li>
											<div id="step2" class="section-block"><strong class="block-title">2) Beban dan
													Belanja</strong></div>
										</li>								
									</p>
									<p>
										Beban diakui pada saat timbulnya kewajiban, terjadinya konsumsi aset, atau
										terjadinya penurunan manfaat ekonomi atau potensi jasa. Belanja adalah
										semua pengeluaran KUD yang mengurangi ekuitas dalam periode tahun yang
										bersangkutan yang tidak akan diperoleh pembayaran kembali oleh pemerintah
										daerah. Belanja diakui pada saat terjadi pengeluaran kas dari KUD. Khusus
										pengeluaran melalui bendahara pengeluaran, pengakuan belanja terjadi pada
										saat pertanggungjawaban atas pengeluaran tersebut disahkan oleh fungsi
										perbendaharaan. Belanja disajikan di muka (<em>face</em>) laporan keuangan
										menurut klasifikasi ekonomi/jenis belanja, sedangkan di Catatan atas
										Laporan Keuangan, belanja disajikan menurut klasifikasi urusan, organisasi,
										program dan kegiatan, dan kelompok.
									</p>
									<p>
										</li>
										<li>
											<div id="step3" class="section-block"><strong class="block-title">3) Pembiayaan
												</strong></div>
									</p>
									<p>
										Pembiayaan adalah seluruh transaksi keuangan pemerintah daerah, baik
										penerimaan maupun pengeluaran yang perlu dibayar atau akan diterima
										kembali, yang dalam penganggaran pemerintah daerah terutama dimaksudkan
										untuk menutup defisit atau memanfaatkan surplus anggaran. Pembiayaan diakui
										pada saat kas diterima pada KUD serta pada saat terjadinya pengeluaran kas
										dari KUD. Akuntansi penerimaan pembiayaan dilaksanakan berdasarkan azas
										bruto, yaitu dengan membukukan penerimaan bruto, dan tidak mencatat jumlah
										netonya (setelah dikompensasikan dengan pengeluaran).
									</p>
									<p>
										</li>
										<li>
											<div id="step4" class="section-block"><strong class="block-title">4) Aset</strong></div>
									</p>
									<p>
										Aset adalah sumber daya ekonomi yang dikuasai dan/atau dimiliki oleh
										pemerintah daerah sebagai akibat dari peristiwa masa lalu dan dari mana
										manfaat ekonomi dan/atau sosial di masa depan diharapkan dapat diperoleh
										oleh pemerintah daerah, serta dapat diukur dalam satuan uang, termasuk
										sumber daya non-keuangan yang diperlukan untuk penyedia jasa bagi
										masyarakat umum dan sumber-sumber daya yang dipelihara karena alasan
										sejarah dan budaya. Dalam pengertian aset ini tidak termasuk sumber daya
										alam seperti hutan, kekayaan di dasar laut, dan kandungan pertambangan.
										Aset diakui pada saat diterima atau pada saat hak kepemilikan berpindah.
									</p>
									<p>
										Aset diklasifikasikan menjadi Aset Lancar, Investasi, Aset Tetap, dan Aset
										lainnya.
									</p>
									<p>
										</li>
										<li>
											<div id="step" class="section-block"><strong class="block-title">a. Aset
													Lancar</strong></div>
									</p>
									<p>
										Aset Lancar mencakup kas dan setara kas yang diharapkan segera untuk
										direalisasikan, dipakai, atau dimiliki untuk dijual dalam waktu 12 (dua
										belas) bulan sejak tanggal pelaporan. Aset lancar ini terdiri dari kas,
										piutang, dan persediaan.
									</p>
									<p>
										Kas dijadikan di neraca dengan menggunakan nilai nominal. Kas dalam bentuk
										valuta asing disajikan di neraca dengan menggunakan kurs tengah Bank
										Indonesia pada tanggal neraca.
									</p>
									<p>
										Piutang dinyatakan dalam neraca menurut nilai yang timbul berdasarkan hak
										yang telah dikeluarkan surat keputusan penagihannya.
									</p>
									<p>
										Persediaan adalah aset lancar dalam bentuk barang atau perlengkapan yang
										dimaksudkan untuk mendukung kegiatan operasional pemerintah daerah, dan
										barang-barang yang dimaksudkan untuk dijual dan/atau diserahkan dalam
										rangka pelayanan kepada masyarakat.
									</p>
									<p>
										Persediaan dicatat di neraca berdasarkan:
									</p>
									<p>
										- harga pembelian terakhir, apabila diperoleh dengan pembelian,
									</p>
									<p>
										- harga standar apabila diperoleh dengan memproduksi sendiri,
									</p>
									<p>
										- harga wajar atau estimasi nilai penjualannya apabila diperoleh dengan
										cara lainnya seperti donasi/rampasan.
									</p>
									<p>
										</li>
										<li>
											<div id="step" class="section-block"><strong class="block-title">b. Investasi</strong></div>
									</p>
									<p>
										Investasi adalah hak yang dimasukkan untuk memperoleh manfaat ekonomik
										seperti bunga, dividen dan royalti, atau manfaat sosial sehingga dapat
										meningkatkan kemampuan pemerintah daerah dalam rangka pelayanan kepada
										masyarakat.
									</p>
									<p>
										Investasi pemerintah daerah diklasifikasikan ke dalam investasi jangka
										pendek dan investasi jangka panjang. Investasi jangka pendek adalah
										investasi yang dapat segera dicairkan dan dimaksudkan untuk dimiliki dalam
										kurun waktu setahun atau kurang. Investasi jangka panjang adalah investasi
										yang dimaksudkan untuk dimiliki selama lebih dari setahun. Penyajian
										investasi pada Neraca Pemerintah Daerah per 31 Desember 200X terbatas pada
										investasi jangka panjang.
									</p>
									<p>
										Investasi jangka panjang dibagi menurut sifat penanaman investasinya, yaitu
										non permanen dan permanen.
									</p>
									<p>
										(i) Investasi non permanen
									</p>
									<p>
										Investasi non permanen adalah investasi jangka panjang yang tidak masuk
										dalam investasi permanen dan dimaksudkan untuk dimilki secara tidak
										berkelanjutan. Investasi non permanen sifatnya bukan penyertaan modal saham
										melainkan berupa pinjaman jangka panjang yang dimaksudkan unutuk pembiayaan
										investasi perusahaan daerah, pemerintah daerah, dan pihak ketiga lainnya.
									</p>
									<p>
										(ii) Investasi permanen
									</p>
									<p>
										Investasi permanen adalah investasi jangka panjang yang dimaksudkan untuk
										dimiliki secara berkelanjutan. Investasi permanen dimaksudkan untuk
										mendapatkan dividen atau menanamkan pengaruh yang signifikan dalam jangka
										panjang.
									</p>
									<p>
										Penilaian investasi diprioritaskan menggunakan metode ekuitas. Jika suatu
										investasi bisa dipastikan tidak akan diperoleh kembali atau terdapat bukti
										bahwa investasi hendak dilepas, maka digunakan metode nilai bersih yang
										direalisasikan. Investasi dalam bentuk pinjaman jangka panjang kepada pihak
										ketiga dan <em>non earning asset</em> atau hanya sebagai bentuk partisipasi
										dalam suatu organisasi seperti penyertaan pada lembaga-lembaga keuangan
										internasional, menggunakan metode biaya.
									</p>
									<p>
										Investasi dalam mata uang asing dicatat berdasarkan kurs tengah Bank
										Indonesia pada tanggal transaksi. Pada setiap tanggal neraca, pos investasi
										dalam mata uang asing dilaporkan ke dalam mata uang rupiah dengan
										menggunakan kurs tengah BI pada tanggal neraca.
									</p>
									<p>
										</li>
										<li>
											<div id="step" class="section-block"><strong class="block-title">a. Aset
													Tetap</strong></div>
									</p>
									<p>
										Aset tetap mencakup seluruh aset yang dimanfaatkan oleh pemerintah daerah
										maupun untuk kepentingan publik yang mempunyai masa manfaat lebih dari satu
										tahun. Aset tetap dilaporkan berdasarkan neraca Perangkat Daerah/PPKD per
										31 Desember 200X pada harga perolehan.
									</p>
									<p>
										Pengakuan aset tetap yang perolehannya didasarkan pada nilai satuan minimum
										kapitalisasi, yaitu sebagai contoh:
									</p>
									<p>
										- Pengeluaran untuk per satuan peralatan dan mesin dan peralatan olah raga
										yang nilainya sama dengan atau lebih dari Rp1.000.000,00 (satu juta
										rupiah).
									</p>
									<p>
										Pengeluaran yang tidak tercakup dalam batasan nilai maksimum kapitalisasi
										tersebut di atas, diperlakukan sebagai biaya kecuali pengeluaran untuk
										tanah, gedung dan bangunan, jalan/irigasi/jaringan, dan aset tetap lainnya
										berupa koleksi perpustakaan dan barang becorak kesenian.
									</p>
									<p>
										Menurut PSAP Nomor 07 tentang Akuntansi Aset Tetap, aset tetap disajikan
										berdasarkan biaya perolehan aset tetap dikurangi akumulasi penyusutan
										(depresiasi).
									</p>
									<p>
										Metode penyusutan aset tetap yang digunakan adalah metode garis lurus (
										<em>straight line method</em>). Semua aset tetap disusutkan, kecuali tanah
										dan konstruksi dalam pengerjaan.
									</p>
									<p>
										</li>
										<li>
											<div id="step" class="section-block"><strong class="block-title">b. Aset
													Lainnya</strong></div>
									</p>
									<p>
										Aset lainnya adalah aset pemerintah daerah selain aset lancar, investasi
										jangka panjang, dan aset tetap. Termasuk dalam Aset Lainnya adalah Tagihan
										Penjualan Angsuran (TPA), Tagihan Tuntutan Ganti Rugi (TGR), yang jatuh
										tempo lebih dari satu tahun, Kemitraan dengan Pihak Ketiga, Dana yang
										Dibatasi Penggunannya, Aset Tak Berwujud, dan Aset Lain-lain.
									</p>
									<p>
										TPA menggambarkan jumlah yang dapat diterima dari penjualan aset pemerintah
										daerah secara angsuran kepada pegawai pemerintah daerah yang dinilai
										sebesar nilai nominal dari kontrak/berita acara penjualan aset yang
										bersangkutan setelah dikurangi dengan angsuran yang telah dibayar oleh
										pegawai ke kas negara atau daftar saldo tagihan penjualan angsuran.
									</p>
									<p>
										TGR merupakan suatu proses yang dilakukan terhadap bendahara/pegawai negeri
										bukan bendahara dengan tujuan untuk menuntut penggantian atas suatu
										kerugian yang diderita oleh pemda sebagai akibat langsung maupun tidak
										langsung dari suatu perbuatan yang melanggar hukum yang dilakukan oleh
										bendahara/pegawai tersebut atau kelalaian dalam pelaksanaan tugasnya.
									</p>
									<p>
										TPA dan TGR yang akan jatuh tempo 12 (dua belas) bulan setengah tanggal
										neraca disajikan sebagai aset lancar.
									</p>
									<p>
										Kemitraan dengan pihak ketiga merupakan perjanjian antara dua pihak atau
										lebih yang mempunyai komitmen untuk melaksanakan kegiatan yang dikendalikan
										bersama dengan menggunakan aset dan/atau hak usaha yang diimiliki.
									</p>
									<p>
										Dana yang Dibatasi Penggunaan merupakan kas atau dana yang alokasinya hanya
										akan dimanfaatkan untuk membiayai kegiatan tertentu.
									</p>
									<p>
										Aset Tak Berwujud merupakan aset nonkeuangan yang dapat diidentifikasi dan
										tidak mempunyai wujud fisik serta dimiliki untuk digunakan dalam
										menghasilkan barang atau jasa atau digunakan untuk tujuan lainnya termasuk
										hak atas kekayaan intelektual. Aset Tak Berwujud meliputi software
										komputer; lisensi dan <em>franchise</em>; hak cipta (<em>copyright</em>),
										paten, <em>goodwill</em>, dan hak lainnya; hak jasa dan operasi Aset Tak
										Berwujud dalam pengembangan.
									</p>
									<p>
										Aset Lain-lain merupakan aset lainnya yang tidak dapat dikategorikan ke
										dalam TPA, Tagihan TGR, Kemitraan dengan Pihak Ketiga, maupun Dana yang
										Dibatasi Penggunannya. Aset lain-lain dapat berupa aset tetap pemerintah
										daerah yang dihentikan dari penggunaan aktif pemerintah daerah, dan aset
										pemerintah daerah yang digunakan oleh Kontraktor Kontrak Kerja sama (KKKS),
										di samping yang dialihkan.
									</p>
									<p>
										</li>
										<li>
											<div id="step" class="section-block"><strong class="block-title">5) Kewajiban</strong></div>
									</p>
									<p>
										Kewajiban adalah utang yang timbul dari masa lalu yang penyelesaiannya
										mengakibatkan aliran keluar sumber daya ekonomi pemerintah daerah. Dalam
										konteks pemerintahan, kewajiban muncul antara lain karena penggunaan sumber
										pembiayaan pinjaman dari masyarakat, lembaga keuangan, entitas pemerintahan
										lain, atau lembaga internasional. Kewajiban pemerintah daerah juga terjadi
										karena perikatan dengan pegawai yang bekerja pada pemerintah daerah. Setiap
										kewajiban dapat dipaksakan menurut hukum sebagai konsekuensi dari kontrak
										yang mengikat atau peraturan perundang-undangan.
									</p>
									<p>
										Kewajiban pemerintah daerah diklasifikasikan ke dalam kewajiban jangka
										pendek dan kewajiban jangka panjang.
									</p>
									<p>
										a. Kewajiban Jangka Pendek
									</p>
									<p>
										Suatu kewajiban diklasifikasikan sebagai kewajiban jangka pendek jika
										diharapkan untuk dibayar untuk jatuh tempo dalam waktu dua belas bulan
										setelah tanggal pelaporan.
									</p>
									<p>
										Kewajiban jangka pendek meliputi Utang Kepada Pihak Ketiga, Utang
										Perhitungan Fihak Ketiga (PFK), Bagian Lancar Utang Jangka Panjang, Utang
										Bunga (<em>accrued </em>i<em>nterest</em>) dan Utang Jangka Pendek Lainnya.
									</p>
									<p>
										b. Kewajiban Jangka Panjang
									</p>
									<p>
										Kewajiban diklasifikasikan sebagai kewajiban jangka panjang jika diharapkan
										untuk dibayar atau jatuh tempo dalam waktu lebih dari dua belas bulan
										setelah tanggal pelaporan.
									</p>
									<p>
										Kewajiban jangka panjang pemerintah daerah terdiri dari utang luar negeri
										dan utang dalam negeri.
									</p>
									<p>
										Kewajiban dicatat sebesar nilai nominal, yaitu sebesar nilai kewajiban
										pemerintah daerah pada saat pertama kali transaksi berlangsung. Aliran
										ekonomi sesudahnya seperti transaksi pembayaran, perubahan penilaian karena
										perubahan kurs mata uang asing, dan perubahan lainnya selain perubahan
										nilai pasar, diperhitungkan dengan menyesuaikan nilai tercatat kewajiban
										tersebut.
									</p>
									<p>
										Utang bunga atas utang pemerintah daerah dicatat sebesar biaya bunga yang
										telah terjadi dan belum dibayar. Bunga dimaksud berasal dari utang
										pemerintah daerah baik dari dalam maupun luar negeri. Utang bunga atas
										utang pemerintah daerah yang belum dibayar diakui pada setiap akhir periode
										pelaporan sebagai bagian dari kewajiban yang berkaitan.
									</p>
									<p>
										Utang PFK dicatat sebesar saldo pungutan/potongan berupa PFK yang belum
										disetorkan kepada pihak lain sampai akhir periode pelaporan.
									</p>
									<p>
										Nilai yang dicantumkan dalam neraca untuk bagian lancar utang jangka
										panjang adalah jumlah yang jatuh tempo dalam waktu dua belas bulan setelah
										tanggal pelaporan.
									</p>
									<p>
										</li>
										<li>
											<div id="step6" class="section-block"><strong class="block-title">6) Kewajiban
													Kontinjensi</strong></div>
									</p>
									<p>
										Kewajiban kontinjensi merupakan kewajiban potensial yang timbul dari
										peristiwa masa lalu dan keberadaannya menjadi pasti dengan terjadinya atau
										tidak terjadinya suatu peristiwa atau lebih pada masa datang yang tidak
										sepenuhnya berada dalam kendali pemerintah daerah, atau kewajiban kini yang
										timbul sebagai akibat masa lalu, tetapi tidak diakui karena kemungkinan
										besar pemerintah daerah tidak mengeluarkan sumber daya yang mengandung
										manfaat ekonomis untuk menyelesaikannya, atau jumlah tersebut tidak dapat
										diukur dengan andal.
									</p>
									<p>
										</li>
										<li>
											<div id="step7" class="section-block"><strong class="block-title">7) Ekuitas
												</strong></div>
									</p>
									<p>
										Ekuitas merupakan kekayaan bersih pemerintah daerah, yaitu selisih antara
										aset dan utang pemerintah daerah pada tanggal laporan. Saldo ekuitas dana
										di Neraca berasal dari saldo akhir ekuitas pada Laporan Perubahan Ekuitas.
									</p>
									<p>
										</li>
										<li>
											<div id="step8" class="section-block"><strong class="block-title">8) Selisih
													Kurs</strong></div>
									</p>
									<p>
										Menurut SAP, transaksi dalam mata uang asing harus dibukukan dalam mata
										uang rupiah dengan menjabarkan jumlah mata uang asing tersebut menurut kurs
										tengah bank sentral pada tanggal transaksi. Utang pemerintah daerah dalam
										mata uang asing dicatat dengan menggunakan kurs tengah bank sentral saat
										terjadinya transaksi. Pada setiap tanggal neraca, pos kewajiban moneter
										dalam mata uang asing dilaporkan ke dalam mata uang rupiah dengan
										menggunakan kurs tengah bank sentral pada tanggal neraca. Kemudian, selisih
										penjabaran pos kewajiban moneter dalam mata uang asing antara tanggal
										transaksi dan tanggal neraca dicatat sebagai kenaikan atau penurunan
										ekuitas periode berjalan.
