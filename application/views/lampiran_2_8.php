										</div>
										</p>
										<p>
											22. Pendapatan nonperpajakan lainnya antara lain dapat berasal dari
											keuntungan penjualan, denda akibat perjanjian/peraturan, bunga/jasa
											perbankan, penerimaan kembali belanja tahun sebelumnya, putusan
											pengadilan/pelanggaran hukum dan penghapusan utang.
										</p>
										<p>
											23. Pendapatan yang berasal dari keuntungan penjualan aset diakui pada saat
											kas diterima oleh entitas, pendapatan yang berasal dari denda akibat
											perjanjian atau peraturan diakui pada saat menjadi hak entitas, pendapatan
											yang berasal dari bunga/jasa perbankan diakui pada saat kas diterima oleh
											entitas, pendapatan yang berasal dari pengembalian kembali belanja tahun
											sebelumnya diakui pada saat kas diterima oleh entitas.
										</p>
										<p>
											24. Untuk pendapatan yang berasal dari penghapusan utang diakui pada saat
											telah ada penetapan dari pemberi pinjaman bahwa utang entitas telah
											dihapuskan oleh pemberi pinjaman.
										</p>
										<p>
									</li>
