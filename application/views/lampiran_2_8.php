										<div id="step15" class="section-block"><strong class="block-title">Pendapatan
												Pemberian Layanan</strong>
										</div>
										</p>
										<p>
											27. Jumlah pendapatan operasional yang berasal dari pemberian layanan yang
											dilakukan oleh pemerintah diukur sebesar nilai yang dibebankan atau
											ditagihkan kepada pengguna.
										</p>
									</li>
									<li>
										<p>
									</li>
									<li>
										<div id="step16" class="section-block"><strong class="block-title">Pendapatan
												Eksploitasi/Pemanfaatan Sumber
												Daya Alam (SDA)</strong></div>
										</p>
										<p>
											28. Terhadap pendapatan operasional yang berasal dari
											pemanfaatan/pengambilan SDA berdasar unitnya, jumlah pendapatannya dinilai
											sebesar jumlah SDA yang diambil/dimanfaatkan dikalikan dengan tarif yang
											telah ditetapkan.
										</p>
										<p>
											29. Dalam hal tarif dan penetapan dengan mata uang asing, jumlah pendapatan
											dicatat dengan mengkonversi menjadi rupiah berdasarkan kurs tengah bank
											sentral pada tanggal penetapan.
										</p>
										<p>
											30. Dalam hal penyetoran baru dilakukan kemudian masih dalam batas waktu
											yang diperkenankan dan terdapat perubahan kurs, jumlah pendapatan
											operasional adalah sebesar jumlah setoran sekaligus untuk koreksi atas
											pencatatan sebelumnya.
										</p>
									</li>
