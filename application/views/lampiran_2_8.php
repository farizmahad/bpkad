											entitas apabila terkait dengan tugas dan fungsi entitas yang bersangkutan.
											Namun, tidak menutup kemungkinan sebagai satuan kerja dari pemerintah,
											pendapatan suatu entitas tidak mempertimbangkan tugas dan fungsinya untuk
