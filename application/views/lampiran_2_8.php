										<p>
											25. Pendapatan yang berasal dari perizinan disajikan sebesar tarif yang
											dipungut oleh pemerintah atas izin yang diberikannya.
										</p>
										<p>
											26. Pemerintah selaku pemberi izin dapat memberikan batas waktu kepada
											penerima izin maupun tidak memberikan batas waktu.
										</p>
									</li>
