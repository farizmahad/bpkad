										</p>
										<p>
											34. Pendapatan operasional yang berasal dari pendapatan sewa dinilai
											sebesar yang menjadi hak entitas pada tahun berjalan. Penghitungan proporsi
											nilai pendapatan dapat menggunakan bulanan maupun tahunan, tergantung
											sistem yang dibangun oleh pemerintah daerah.
										</p>
										<p>
											35. Pendapatan operasional yang berasal dari kerja sama pemanfaatan,
											dinilai sebesar jumlah yang menjadi hak entitas pemerintah daerah yang
											bersangkutan pada tahun berjalan. Penghitungan proporsi nilai pendapatan
											dapat menggunakan bulanan maupun tahunan, tergantung sistem yang dibangun
											oleh pemerintah daerah.
										</p>
