												Investasi</strong></div>
										</p>
										<p>
											31. Hasil investasi yang diperoleh dari investasi jangka pendek bunga
											deposito dinilai sebesar bunga deposito yang diterima oleh entitas. Untuk
											hasil investasi dari bunga obligasi dinilai sebesar bunga obligasi yang
											menjadi hak entitas berdasarkan sertifikat obligasi yang dimiliki oleh
											entitas.
										</p>
										<p>
											32. Hasil investasi jangka pendek yang berasal dari penyertaan jangka
											pendek berupa dividen tunai (cash dividend) dinilai sebesar jumlah yang
											akan diterima oleh pemerintah yang diumumkan pada saat Rapat Umum Pemegang
											Saham (RUPS).
										</p>
										<p>
											33. Pendapatan dari investasi jangka panjang penyertaan modal pemerintah,
											apabila menggunakan metode biaya, dinilai sebesar bagian tunai yang akan
											diterima oleh entitas pada saat diumumkan dalam RUPS. Sementara itu apabila
											menggunakan metode ekuitas disamping dinilai sebesar bagian tunai yang akan
											diterima oleh entitas pada saat diumumkan dalam RUPS, pendapatan tersebut
											juga akan mengurangi ekuitas entitas pada penyertaan tersebut.
										</p>
