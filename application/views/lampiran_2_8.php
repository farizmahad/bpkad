										<p>
											36. Pendapatan operasional yang berasal dari keuntungan penjualan atau
											pertukaran aset diukur sebesar selisih positif antara kas dan/atau nilai
											aset yang diterima dikurangi dengan nilai buku buku aset yang dijual atau
											diserahkan.
										</p>
										<p>
											37. Pendapatan yang berasal dari denda akibat perjanjian atau peraturan
											pemerintah dinilai sebesar nilai penetapan.
										</p>
										<p>
											38. Untuk pendapatan bunga/jasa perbankan dan pendapatan dari pengembalian
											belanja tahun sebelumnya diakui sebesar yang telah diterima oleh entitas.
										</p>
										<p>
											39. Pendapatan yang berasal dari putusan pengadilan/pelanggaran hukum
											dinilai sebesar penetapan putusan.
										</p>
										<p>
											40. Pendapatan yang berasal dari penghapusan utang, dinilai sebesar nilai
											utang yang dihapuskan oleh pemberi pinjaman. Bisa terjadi penghapusan utang
											dapat berasal dari usaha yang melebihi satu periode, namun mengingat
											prinsip keandalan, pendapatan dari penghapusan utang ini diakui sebesar
											nilai penghapusan pada tahun berkenaan.
										</p>
