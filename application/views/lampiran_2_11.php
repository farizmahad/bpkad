											entitas pemerintah yang bertanggung jawab/melaksanakan kegiatan terkait
											dengan Bantuan Sosial harus mengidentifikasikan, mengakui, mengukur dan
											menyajikan Beban Bantuan Sosial pada Laporan Operasional serta
											mengungkapkannya secara memadai pada Catatan atas Laporan Keuangan adalah
