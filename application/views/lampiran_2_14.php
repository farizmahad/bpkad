											a) Kualitas lancar 0,5% (nol koma lima perseratus) dari nilai dana bergulir
											kualitas lancar;
										</p>
										<p>
											b) Kualitas kurang lancar sebesar 10% (sepuluh perseratus) dari nilai dana
											bergulir kualitas kurang lancar;
										</p>
