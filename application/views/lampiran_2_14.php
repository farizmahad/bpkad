													Satuan kerja mengajukan pencairan dana kepada Bendahara Umum Daerah
													(BUD);
												</em>
											</strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>c)
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Penyaluran dana dilakukan melalui lembaga keuangan bank yang
													berperan sebagai chanelling agency sesuai dengan perjanjian yang
													dilakukan oleh satuan kerja pemerintah daerah dengan lembaga
													keuangan bank tersebut. Lembaga keuangan bank hanya berfungsi untuk
													menyalurkan dana bergulir kepada penerima dana bergulir dan tidak
													mempunyai tanggungjawab menetapkan penerima dana bergulir;
												</em>
											</strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>d)
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Dana yang disalurkan tersebut merupakan pinjaman yang harus
													dikembalikan oleh masyarakat kepada satuan kerja melalui lembaga
													keuangan bank;
												</em>
											</strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>e)
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Satuan kerja melakukan pengelolaan dana dan melakukan pengendalian
													penagihan dana dari masyarakat, menyalurkan kembali dana tersebut
													kepada masyarakat/kelompok masyarakat, melaporkan dan
													mempertanggungjawabkan dana tersebut.
												</em>
											</strong></div>
										</p>
										<p>
											38. Pengembalian dana kepada satuan kerja pengelola dana bergulir dapat
											dilakukan dengan cicilan atau pengembalian sekaligus berupa pinjaman pokok
											dan bunga.
										</p>
										<p>
											39. Besaran cicilan pokok yang dibayar oleh masyarakat adalah sebesar
											pinjaman dana yang diterima dengan memperhitungkan jangka waktu
											pengembalian. Besaran bunga yang dibayar kepada satuan kerja pengelola dana
											bergulir adalah sebesar suku bunga yang ditetapkan oleh bank sebagai
											penyalur.
										</p>
									</li>
									<li>
										<p>
									</li>
									<li>
										<div id="step13" class="section-block"><strong class="block-title">PELAPORAN
												PENAGIHAN DANA BERGULIR</strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>40.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Penerimaan cicilan pokok dana bergulir tidak dicatat dan dilaporkan
													dalam laporan keuangan pemerintah daerah tetapi cukup diungkapkan
													dalam Catatan atas Laporan Keuangan.
												</em>
											</strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>41.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Bendahara Umum Daerah (BUD) dapat menarik pokok dana bergulir yang
													terdapat pada satuan kerja pengelola dana bergulir.
												</em>
											</strong></div>
										</p>
										<p>
											42. Penarikan atau penyetoran pokok dana bergulir ke Rekening Kas Umum
											Daerah akan dicatat sebagai penerimaan pembiayaan dalam Laporan Realisasi
											Anggaran dan pengurangan dana bergulir dalam Neraca.
										</p>
										<p>
											43. Kas yang diterima satuan kerja pengelola dana bergulir baik yang
											berasal dari pokok pinjaman dan pendapatan bunga dana bergulir dapat
											dikelola langsung satuan kerja pengelola dana bergulir tanpa perlu disetor
											ke Rekening Kas Umum Daerah kecuali pendapatan jasa giro rekening
											pengelolaan dana bergulir harus disetorkan ke Rekening Kas Umum Daerah.
										</p>
										<p>
											44. Bendahara Umum Daerah (BUD) mencatat pendapatan jasa giro dana bergulir
											sebagai Lain-lain Pendapatan Asli Daerah.
										</p>
										<p>
											45. Cicilan pokok dan pendapatan bunga dana bergulir dapat digulirkan
											kembali kepada masyarakat sehingga aset dana bergulir yang dikelola oleh
											satuan kerja pengelola dana bergulir bertambah.
										</p>
									</li>
									<li>
										<p>
									</li>
									<li>
										<div id="step14" class="section-block"><strong class="block-title">PENYAJIAN DAN
												PENGUNGKAPAN DANA BERGULIR</strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>46.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Pengeluaran dana bergulir diakui sebagai pengeluaran pembiayaan
													yang disajikan dalam Laporan Realisasi Anggaran dan Laporan Arus
													Kas. Pengeluaran pembiayaan tersebut dicatat sebesar jumlah kas
													yang dikeluarkan dalam rangka perolehan dana bergulir.
												</em>
											</strong></div>
										</p>
										<p>
											47. Dana bergulir disajikan di neraca sebagai Investasi Jangka
											Panjang-Investasi Non Permanen-Dana Bergulir sebesar harga perolehan tetapi
											secara periodik pemerintah daerah melakukan penyesuaian terhadap dana
											bergulir sehingga nilai dana bergulir yang tercatat di neraca menggambarkan
											nilai bersih yang dapat direalisasikan (<em>net realizable value</em>).
										</p>
										<p>
											48. Nilai bersih yang dapat direalisasikan dapat diperoleh dengan
											mengurangi harga perolehan dana bergulir dengan dana bergulir yang
											diragukan tertagih ditambah dengan perguliran dana yang berasal dari
											pendapatan dana bergulir.
										</p>
										<p>
											49. Dana bergulir diragukan tertagih merupakan penyisihan dana bergulir
											tidak tertagih yang diperoleh sebesar prosentase tertentu dari akun dana
											bergulir berdasarkan penggolongan kualitas dana bergulir.
										</p>
										<p>
											50. Kualitas dana bergulir adalah tingkat ketertagihan dana bergulir
											berdasarkan kepatuhan membayar kewajiban oleh debitur. Kualitas dana
											bergulir dikelompokan menjadi 4 (empat) dengan klasifikasi sebagai berikut
											:
										</p>
										<p>
											a) Kualitas Lancar;
										</p>
										<p>
											b) Kualitas Kurang Lancar;
										</p>
										<p>
											c) Kualitas Diragukan;
										</p>
										<p>
											d) Kualitas Macet.
										</p>
										<p>
											51. Penggolongan kualitas dana bergulir dilakukan dengan ketentuan :
										</p>
										<p>
											a) Kualitas lancar dengan kriteria :
										</p>
										<p>
											· Umur dana bergulir kurang dari 1 (satu) tahun; dan/atau
										</p>
										<ul>
											<li>
												Debitur kooperatif; dan/atau
											</li>
											<li>
												Debitur likuid.
											</li>
										</ul>
										<p>
											b) Kualitas kurang lancar dengan kriteria :
										</p>
										<p>
											· Umur dana bergulir 1 (satu) sampai dengan 2 (dua) tahun; dan atau
										</p>
										<ul>
											<li>
												Debitur kurang kooperatif; dan/atau
											</li>
										</ul>
										<p>
											c) Kualitas diragukan dengan kriteria :
										</p>
										<p>
											· Umur dana bergulir di atas 2 (dua) sampai dengan 5 (lima) tahun; dan/atau
										</p>
										<ul>
											<li>
												Debitur tidak kooperatif; dan/atau
											</li>
										</ul>
										<p>
											· Debitur mengalami kesulitan likuiditas.
										</p>
										<p>
											d) Kualitas macet dengan kriteria :
										</p>
										<p>
											· Umur dana bergulir diatas 5 (lima) tahun; dan/atau
										</p>
										<ul>
											<li>
												Debitur tidak ditemukan; dan/atau
											</li>
										</ul>
										<p>
											· Debitur bangkrut/meninggal dunia; dan/atau
										</p>
										<ul>
											<li>
												Debitur mengalami musibah (<em>force majeure</em>).
											</li>
										</ul>
