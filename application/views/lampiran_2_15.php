												<em>
													Tanah disajikan di neraca dalam kelompok aset tetap sebesar biaya
													perolehan atau nilai wajar pada saat tanah diperoleh.
												</em>
											</strong></div>
										</p>
										<p>
											46. Dalam hal tanah belum ada bukti kepemilikan yang sah, namun dikuasai
											dan/atau digunakan oleh pemerintah daerah, maka tanah tersebut tetap harus
											dicatat dan disajikan sebagai aset tetap tanah pada neraca pemerintah
											daerah serta diungkapkan secara memadai dalam Catatan atas Laporan
											Keuangan.
										</p>
										<p>
											47. Dalam hal tanah dimiliki oleh pemerintah daerah, namun dikuasai
											dan/atau digunakan oleh pihak lain, maka tanah tersebut harus tetap dicatat
											dan disajikan sebagai aset tetap tanah pada neraca pemerintah daerah, serta
											diungkapkan secara memadai dalam Catatan atas Laporan Keuangan bahwa tanah
											tersebut dikuasai atau digunakan oleh pihak lain.
										</p>
										<p>
											48. Dalam hal tanah dimiliki oleh suatu entitas pemerintah, namun dikuasai
											dan/atau digunakan oleh entitas pemerintah yang lain, maka tanah tersebut
											dicatat dan disajikan pada neraca entitas pemerintah yang mempunyai bukti
											kepemilikan serta diungkapkan secara memadai dalam Catatan atas Laporan
											Keuangan. Entitas pemerintah yang menguasai dan/atau menggunakan tanah
											cukup mengungkapkan tanah tersebut secara memadai dalam Catatan atas
											Laporan Keuangan.
										</p>
										<p>
											49. Penyajian dan pengungkapan tanah yang masih dalam sengketa atau proses
											pengadilan :
										</p>
										<p>
											a) Dalam hal belum ada bukti kepemilikan tanah yang sah, tanah tersebut
											dikuasai dan/atau digunakan oleh pemerintah daerah, maka tanah tersebut
											tetap harus dicatat dan disajikan sebagai aset tetap tanah pada neraca
											pemerintah daerah serta diungkapkan secara memadai dalam Catatan atas
											Laporan Keuangan.
										</p>
										<p>
											b) Dalam hal pemerintah daerah belum mempunyai bukti kepemilikan tanah yang
											sah, tanah tersebut dikuasai dan/atau digunakan oleh pihak lain, maka tanah
											tersebut dicatat dan disajikan sebagai aset tetap tanah pada neraca
											pemerintah daerah serta diungkapkan secara memadai dalam Catatan atas
											Laporan Keuangan.
										</p>
										<p>
											c) Dalam hal bukti kepemilikan tanah ganda, namun tanah tersebut dikuasai
											dan/atau digunakan oleh pemerintah daerah, maka tanah tersebut tetap harus
											dicatat dan disajikan sebagai aset tetap tanah pada neraca pemerintah
											daerah serta diungkapkan secara memadai dalam Catatan atas Laporan
											Keuangan.
										</p>
										<p>
											d) Dalam hal bukti kepemilikan tanah ganda, namun tanah tersebut dikuasai
											dan/atau digunakan oleh lain, maka tanah tersebut tetap harus dicatat dan
											disajikan sebagai aset tetap tanah pada neraca pemerintah daerah, namun
											adanya sertifikat ganda harus diungkapkan secara memadai dalam Catatan atas
											Laporan Keuangan.
										</p>
										<p>
											50. Tanah yang digunakan/dipakai oleh pemerintah daerah yang berstatus
											tanah wakaf tidak disajikan dan dilaporkan sebagai aset tetap tanah pada
											neraca pemerintah daerah, melainkan cukup diungkapkan secara memadai pada
											Catatan atas Laporan Keuangan (CaLK).
