									</li>
									<li>
										<div id="step11" class="section-block"><strong class="block-title">Pengukuran
												Tanah</strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>41.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Tanah dinilai dengan biaya perolehan. Apabila penilaian tanah
													dengan menggunakan biaya perolehan tidak memungkinkan, maka nilai
													tanah didasarkan pada nilai wajar saat perolehan.
												</em>
											</strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"></strong></div>
										</p>
										<p>
											42. Biaya yang terkait dengan peningkatan bukti kepemilikan tanah, misalnya
											dari status tanah girik menjadi hak milik, dikapitalisasi sebagai biaya
											perolehan tanah.
										</p>
										<p>
											43. Biaya yang timbul atas penyelesaian sengketa tanah, seperti biaya
											pengadilan dan pengacara tidak dikapitalisasi sebagai biaya perolehan
											tanah.
										</p>
										<p>
											44. Pengukuran suatu aset tetap harus memperhatikan kebijakan pemerintah
											daerah mengenai ketentuan nilai satuan minimum kapitalisasi aset tetap.
											Namun, untuk aset tetap berupa tanah, berapapun nilai perolehannya
											seluruhnya dikapitalisasi sebagai nilai tanah.
										</p>
