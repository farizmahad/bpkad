											yang tak terbatas. Aset bersejarah biasanya dibuktikan dengan peraturan
											perundang-undangan yang berlaku.
										</p>
										<p>
											160. Pemerintah Kota Cimahi mungkin mempunyai banyak aset bersejarah yang
											diperoleh selama bertahun-tahun dan dengan cara perolehan beragam termasuk
											pembelian, donasi, warisan, rampasan, ataupun sitaan. Aset ini jarang
											dikuasai dikarenakan alasan kemampuannya untuk menghasilkan aliran kas
											masuk, dan akan mempunyai masalah sosial dan hukum bila memanfaatkannya
											untuk tujuan tersebut.
										</p>
										<p>
											161. Aset bersejarah harus disajikan dalam bentuk unit, misalnya jumlah
											unit koleksi yang dimiliki atau jumlah unit monumen, dalam Catatan atas
											Laporan Keuangan dengan tanpa nilai. .
										</p>
										<p>
											162. Biaya untuk perolehan, konstruksi, peningkatan, rekonstruksi harus
											dibebankan sebagai belanja tahun terjadinya pengeluaran tersebut. Biaya
											tersebut termasuk seluruh biaya yang berlangsung untuk menjadikan aset
											bersejarah tersebut dalam kondisi dan lokasi yang ada pada periode
											berjalan.
										</p>
										<p>
											163. Beberapa aset bersejarah juga memberikan potensi manfaat lainnya
											kepada Pemerintah Kota Cimahi selain nilai sejarahnya, sebagai contoh
											bangunan bersejarah digunakan untuk ruang perkantoran. Untuk kasus
											tersebut, aset ini akan diterapkan prinsip-prinsip yang sama seperti aset
											tetap lainnya.
										</p>
										<p>
											164. Untuk aset bersejarah lainnya, potensi manfaatnya terbatas pada
											karakteristik sejarahnya, sebagai contoh monumen dan reruntuhan (ruins).
										</p>
