										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>32.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Tanah diakui pertama kali sebesar biaya perolehan. Biaya perolehan
													mencakup harga pembelian atau biaya pembebasan tanah, biaya yang
													dikeluarkan dalam rangka memperoleh hak, biaya pematangan,
													pengukuran, penimbunan, dan biaya lainnya yang dikeluarkan maupun
													yang masih harus dikeluarkan sampai tanah tersebut siap pakai.
													Nilai tanah juga meliputi nilai bangunan tua yang terletak pada
													tanah yang dibeli tersebut jika bangunan tua tersebut dimaksudkan
													untuk dimusnahkan.
												</em>
											</strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong
												class="block-title"><em></em></strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong
												class="block-title"><em></em></strong></div>
										</p>
										<p>
											<em>33. </em>
											Tanah dapat diperoleh melalui pembelian, pertukaran aset, dan hibah/donasi.
											Tanah yang diperoleh melalui pembelian diakui berdasarkan nilai belanja
											yang telah dikeluarkan.
										</p>
										<p>
											<em>34. </em>
											Perolehan tanah melalui hibah/donasi tidak melibatkan pengeluaran uang
											secara riil dalam bentuk belanja. Pengakuan tanah dilakukan dengan cara
											menaksir nilai tanah tersebut menurut nilai wajar.
										</p>
										<p>
											<em>35. </em>
											Apabila perolehan tanah pemerintah daerah dilakukan oleh panitia pengadaan,
											maka termasuk dalam harga perolehan tanah adalah honor panitia
											pengadaan/pembebasan tanah, belanja barang dan belanja perjalanan dinas
											dalam rangka perolehan tanah teresebut.
										</p>
										<p>
											<em>36. </em>
											Tanah dapat diakui sebagai aset tetap apabila memenuhi 4 (empat) kriteria
											berikut:
										</p>
										<p>
											a) Mempunyai masa manfaat lebih dari 12 (dua belas) bulan;
										</p>
										<p>
											b) Biaya perolehan aset tetap dapat diukur secara andal;
										</p>
										<p>
											c) Tidak dimaksudkan untuk dijual;
										</p>
										<p>
											d) Diperoleh dengan maksud untuk digunakan.
										</p>
										<p>
											<em>37. </em>
											Pengadaan tanah pemerintah daerah yang sejak semula dimaksudkan untuk
											diserahkan kepada pihak lain tidak disajikan sebagai aset tetap tanah,
											melainkan disajikan sebagai persediaan.
										</p>
										<p>
											<em>38. </em>
											Pengakuan aset tetap akan sangat andal bila aset tetap telah diterima atau
											diserahkan hak kepemilikannya dan/atau pada saat penguasaannya berpindah.
											Hak kepemilikan tanah didasarkan pada bukti kepemilikan tanah yang sah
											berupa sertifikat, misalnya Sertifikat Hak Milik (SHM), Sertifikat Hak Guna
											Bangunan (SHGB), dan Sertifikat Pengelolaan Lahan (SPL).
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>39.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Tanah yang dimiliki dan/atau dikuasai pemerintah daerah tidak
													diperlakukan secara khusus, dan pada prinsipnya mengikuti ketentuan
													seperti yang diatur pada kebijakan akuntansi tentang aset tetap.
												</em>
											</strong></div>
										</p>
										<p>
											<em>40. </em>
											Tidak seperi insitusi nonpemerintah, pemerintah daerah tidak dibatasi satu
											periode tertentu untuk kepemilikan dan/atau penguasaan tanah yang dapat
											berbentuk hak pakai, hak pengelolaan, dan hak atas tanah lainnya yang
											dimungkinkan oleh peraturan perundang-undangan yang berlaku. Oleh karena
											itu, setelah perolehan awal, pemerintah daerah tidak memerlukan biaya untuk
											mempertahankan hak atas tanah tersebut. Tanah memenuhi definisi aset tetap
											dan harus diperlakukan sesuai dengan prinsip-prinsip yang ada pada
											kebijakan ini.
										</p>
