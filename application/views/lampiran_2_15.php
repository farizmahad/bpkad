												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Aset tetap yang diperoleh dari sumbangan (donasi) harus dicatat
													sebesar nilai wajar pada saat perolehan.
												</em>
											</strong></div>
