										<p>
											a) Penerimaan pembiayaan adalah semua penerimaan Rekening Kas Umum Daerah
											antara lain berasal dari penerimaan pinjaman, hasil privatisasi perusahaan
											daerah, penjualan investasi permanen, dan pencairan dana cadangan.
										</p>
										<p>
											b) Pengeluaran pembiayaan adalah pengeluaran semua Rekening Kas Umum
											Daerah, antara lain penyertaan modal pemerintah daerah, pembayaran bagian
											lancar utang jangka panjang, pembayaran utang jangka pendek, dan
											pembentukan dana cadangan.
										</p>
									</li>
									<li>
										<p>
											<div id="step1" class="section-block"><strong
													class="block-title">Pengakuan</strong></div>
										</p>
										<p>
											58. Penerimaan pembiayaan diakui pada saat diterima pada Rekening Kas Umum
											Daerah.
										</p>
										<p>
											59. Pengeluaran pembiayaan diakui pada saat dikeluarkan dari Rekening Kas
											Umum Daerah.
										</p>
									</li>
									<li>
										<div id="step1" class="section-block"><strong class="block-title">
												Pengukuran
											</strong></div>
										<p>
											60. Pengukuran pembiayaan menggunakan mata uang rupiah berdasarkan nilai
											sekarang kas yang diterima oleh nilai sekarang kas yang dikeluarkan.
										</p>
										<p>
											<em>61. </em>
											Akuntansi penerimaan pembiayaan dilaksanakan berdasarkan azas bruto, yaitu
											dengan membukukan penerimaan bruto, dan tidak mencatat jumlah netonya (setelah dikompensasikan dengan pengeluaran).
										</p>
										<p>
											62. Pencairan Dana Cadangan mengurangi Dana Cadangan yang bersangkutan.
										</p>
										<p>
											63. Pembentukan Dana Cadangan menambah Dana Cadangan yang bersangkutan.
											Hasil-hasil yang diperoleh dari pengelolaan Dana Cadangan dipemerintah
											daerah merupakan penambah Dana Cadangan. Hasil tersebut dicatat sebagai
											pendapatan-LRA dalam pos pendapatan asli daerah lainnya.
										</p>
									</li>
									<li>
										<p>
											<div id="step1" class="section-block"><strong class="block-title">Penyajian
													dan Pengungkapan</strong></div>
										</p>
										<p>
											64. Penerimaan dan pengeluaran pembiayaan pemerintah daerah disajikan dalam
											Laporan Realisasi Anggaran (LRA). Dalam pengungkapan pada Catatan atas
											Laporan Keuangan harus diuangkapkan pula hal-hal sebagai berikut :
										</p>
										<p>
											a) Penerimaan dan pengeluaran pembiayaan tahun berkenaan setelah tanggal
											berakhirnya tahun anggaran;
										</p>
										<p>
											b) Penjelasan landasan hukum mengenai pemberian pinjaman,
											pembentukan/pencairan dana cadangan, penjualan aset daerah yang dipisahkan,
											penyertaan modal pemerintah daerah; dan
										</p>
										<p>
											c) Informasi lainnya yang dianggap perlu.
										</p>
									</li>
									<li>
										<p>
											<div id="step1" class="section-block"><strong class="block-title">AKUNTANSI
													PEMBIAYAAN NETO</strong></div>
										</p>
										<p>
											65. Pembiayaan neto adalah selisih antara penerimaan pembiayaan setelah
											dikurangi pengeluaran pembiayaan dalam periode tahun anggaran tertentu.
										</p>
										<p>
											66. Selisih lebih/kurang antara penerimaan dan pengeluaran pembiayaan
											selama satu periode pelaporan dicatat dalam Pembiayaan<em> </em>Neto.
