									<li>
										<div id="step" class="section-block"><strong
												class="block-title"><em>Penghapustagihan (write off);
													dan</em></strong></div>
										</p>
										<p>
											60. Penghapusbukuan piutang adalah kebijakan intern manajemen yang
											merupakan proses dan keputusan akuntansi yang berlaku agar nilai piutang
											dapat dipertahankan sesuai dengan <em>net realizable value</em>-nya.
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>61.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Penghapusbukuan piutang merupakan penghapusan piutang secara
													bersyarat karena Penghapusbukuan piutang tidak otomatis menghapus
													kegiatan penagihan. Apabila dihapusbukukan, berarti pengalihan
													pencatatan dari intracomptabel menjadi ekstracomptabel. Diperlukan
													laporan off balance sheet tentang piutang yang dihapusbukukan namun
													secara yuridis formil belum dihapus, dan atau belum diberitahukan
													kepada debitur.
												</em>
											</strong></div>
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>62.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Penghapusbukuan piutang merupakan konsekuensi penghapustagihan
													piutang. Penghapusbukuan piutang dibuat berdasarkan berita acara
													atau keputusan pejabat yang berwenang untuk menghapustagih piutang.
													Keputusan dan/atau Berita Acara merupakan dokumen yang sah untuk
													bukti akuntansi penghapusbukuan.
												</em>
											</strong></div>
										</p>
										<p>
											<em>63. </em>
											Kriteria Penghapusbukuan Piutang adalah sebagai berikut :<em></em>
										</p>
										<p>
											<em>a) </em>
											Penghapusbukuan harus memberi manfaat yang lebih besar daripada kerugian
											penghapusbukuan;<em></em>
										</p>
										<p>
											· Memberi gambaran objektif tentang kemampuan keuangan entitas akuntansi
											dan entitas pelaporan.<em></em>
										</p>
										<p>
											· Memberi gambaran ekuitas lebih objektif tentang penurunan ekuitas.
											<em></em>
										</p>
										<p>
											· Mengurangi beban administrasi/akuntansi untuk mencatat hal-hal yang tak
											mungkin terealisasi tagihannya.<em></em>
										</p>
										<p>
											<em>b) </em>
											Perlu kajian mendalam tentang dampak hukum dari penghapusbukuan pada neraca
											pemerintah daerah, apabila perlu, sebelum difinalisasi dan diajukan kepada
											pengambilkeputusan penghapusbukuan.<em></em>
										</p>
										<p>
											<em>c) </em>
											Penghapusbukuan berdasarkan keputusan formal otoritas tertinggi yang
											berwenang menyatakan hapus tagih perdata atau hapus buku ( <em>write
												off</em>). Pengambil keputusan penghapusbukuan melakukan
											keputusan reaktif (tidak berinisiatif), berdasar suatu sistem nominasi
											untuk dihapusbukukan atas usulan berjenjang yang bertugas melakukan
											analisis dan usulan penghapusbukuan tersebut.<em></em>
										</p>
										<p>
											<em>64. </em>
											Berdasarkan keputusan penghapusbukuan piutang sebagai dokumen standar,
											piutang tersebut dihapuskan dari pembukuan dengan membuat memo penyesuaian.
											<em></em>
										</p>
										<p>
											65. Dalam hal piutang adalah berupa Tuntutan Ganti Kerugian Daerah,
											penghapusbukuan piutang hanya dapat dilakukan setelah terbitnya rekomendasi
											penghapusan secara bersyarat dari Badan Pemeriksa Keuangan (BPK).
										</p>
										<p>
											66. Penghapusbukuan piutang harus diungkapkan secara cukup dalam Catatan
											atas Laporan Keuangan agar lebih informatif. Informasi yang perlu
											diungkapkan misalnya jenis piutang, nama debitur, nilai piutang, nomor dan
											tanggal keputusan penghapusan piutang, dan penjelasan lainnya yang dianggap
											perlu.
										</p>
										<p>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title"><em>67.
												</em></strong></div>
									</li>
									<li>
										<div id="step" class="section-block"><strong class="block-title">
												<em>
													Penghapustagihan piutang daerah adalah penghapusan hak tagih daerah
													secara mutlak. Penghapustagihan diajukan setelah lewat waktu 2
													(dua) tahun sejak tanggal penetapan penghapusan secara bersyarat.
												</em>
											</strong></div>
										</p>
										<p>
											68. Kriteria Penghapustagihan Piutang baik sebagian atau seluruhnya adalah
											sebagai berikut :
										</p>
										<p>
											a) Penghapustagihan karena mengingat jasa-jasa debitur kepada daerah, untuk
											menolong debitur dari keterpurukan yang lebih dalam, misalnya kredit UKM
											yang tidak mampu membayar;
										</p>
										<p>
											b) Penghapustagihan sebagai suatu sikap menyejukan, membuat citra penagih
											menjadi lebih baik, memperoleh dukungan moril lebih luas;
										</p>
										<p>
											c) Penghapustagihan sebagai sikap berhenti menagih, menggambarkan situasi
											tak mungkin tertagih melihat kondisi pihak tertagih.
										</p>
										<p>
											d) Penghapustagihan untuk restrukturisasi penyehatan utang, misalnya
											penghapusan denda.
										</p>
										<p>
											e) Penghapustagihan setelah semua upaya lain gagal atau tidak mungkin
											diterapkan, misalnya jaminan dilelang.
										</p>
										<p>
											f) Penghapustagihan sesuai hukum perdata umumnya, hukum kepailitan, hukum
											industri, dan hukum pajak.
										</p>
										<p>
											g) Penghapustagihan secara hukum sulit atau tidak mungkin dibatalkan,
											apabila telah diputuskan dan diberlakukan, kecuali cacat hukum.
										</p>
										<p>
											69. Suatu piutang yang telah dihapusbukukan, ada kemungkinan diterima
											pembayarannya karena timbulnya kesadaran dan rasa tanggung jawab debitur.
											Pembayaran kembali piutang ini dicatat sebagai penerimaan pendapatan
											periode yang bersangkutan.
										</p>
