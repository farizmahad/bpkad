                                    <li>
                                        <div id="step24" class="section-block"><strong class="block-title">Laporan
                                                Realisasi
                                                Anggaran</strong>

                                        </div>
                                    </li>
                                    <li>
                                        <div id="step24" class="section-block"><strong class="block-title">35. Laporan
                                                Realisasi
                                                Anggaran
                                            </strong>Perangkat Daerah
                                            /PPKD/Pemda merupakan laporan yang menyajikan ikhtisar sumber, alokasi dan
                                            pemakaian sumber daya keuangan yang dikelola oleh Perangkat Daerah
                                            /Pemerintah Daerah, yang menggambarkan perbandingan antara realisasi dan
                                            anggarannya dalam satu periode pelaporan. Unsur yang dicakup secara
                                            langsung oleh Laporan Realisasi Anggaran terdiri dari pendapatan-LRA,
                                            belanja, transfer, dan pembiayaan. Masing-masing unsur didefinisikan
                                            sebagai berikut:
                                            </p>
                                            <p>
                                                (a) Pendapatan - LRA adalah penerimaan oleh Bendahara Umum Daerah atau
                                                oleh
                                                entitas pemerintah daerah lainnya yang menambah Saldo Anggaran Lebih
                                                dalam
                                                periode tahun anggaran yang bersangkutan yang menjadi hak pemerintah
                                                daerah, dan tidak perlu dibayar kembali oleh pemerintah daerah.
                                            </p>
                                            <p>
                                                (b) Belanja adalah semua pengeluaran oleh Bendahara Umum Daerah yang
                                                mengurangi Saldo Anggaran Lebih dalam periode tahun anggaran
                                                bersangkutan
                                                yang tidak akan diperoleh pembayarannya kembali oleh pemerintah daerah.
                                            </p>
                                            <p>
                                                (c) Transfer adalah penerimaan/pengeluaran uang dari suatu entitas
                                                pelaporan dari/kepada entitas pelaporan lain, termasuk dana perimbangan
                                                dan
                                                dana bagi hasil.
                                            </p>
                                            <p>
                                                (d) Pembiayaan (<em>financing</em>) adalah setiap penerimaan/pengeluaran
                                                yang tidak berpengaruh pada kekayaan bersih entitas pelaporan yang perlu
                                                dibayar kembali dan/atau akan diterima kembali, baik pada tahun anggaran
                                                bersangkutan maupun tahun-tahun anggaran berikutnya, yang dalam
                                                penganggaran pemerintah daerah terutama dimaksudkan untuk menutup
                                                defisit
                                                atau memanfaatkan surplus tahun anggaran.
                                            </p>
                                            <p>
                                                (e) Penerimaan pembiayaan antara lain dapat berasal dari pinjaman dan
                                                hasil
                                                divestasi. Pengeluaran pembiayaan antara lain digunakan untuk pembayaran
                                                kembali pokok pinjaman, pemberian pinjaman kepada entitas lain, dan
                                                penyertaan modal oleh pemerintah daerah.
                                            </p>

                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step25" class="section-block"><strong class="block-title">Laporan
                                                Perubahan
                                                Saldo
                                                Anggaran Berlebih</strong>
                                            </p>
                                            <p>
                                                36. Laporan Perubahan Saldo Anggaran Lebih menyajikan informasi kenaikan
                                                atau penurunan Saldo Anggaran Lebih tahun pelaporan dibandingkan dengan
                                                tahun sebelumnya.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step26" class="section-block"><strong class="block-title">Neraca
                                            </strong>

                                        </div>
                                    </li>
                                    <li>
                                        <div id="step" class="section-block"><strong class="block-title">37.
                                                Neraca</strong>
                                            Perangkat
                                            Daerah /PPKD/Pemerintah Daerah
                                            merupakan laporan yang menggambarkan posisi keuangan Perangkat
                                            Daerah/PPKD/Pemerintah Daerah mengenai aset, kewajiban dan ekuitas pada
                                            tanggal tertentu.
                                            </p>
                                            <p>
                                                38. Unsur yang dicakup oleh neraca terdiri dari aset, kewajiban, dan
                                                ekuitas. Masing-masing unsur didefinisikan sebagai berikut:
                                            </p>
                                            <p>
                                                (a) Aset adalah sumber daya ekonomi yang dikuasai dan/atau dimiliki oleh
                                                pemerintah daerah sebagai akibat dari peristiwa masa lalu dan dari mana
                                                manfaat ekonomi dan/atau sosial di masa depan diharapkan dapat diperoleh
                                                oleh pemerintah daerah, serta dapat diukur dalam satuan uang, termasuk
                                                sumber daya nonkeuangan yang diperlukan untuk penyediaan jasa bagi
                                                masyarakat umum dan sumber-sumber daya yang dipelihara karena alasan
                                                sejarah dan budaya.
                                            </p>
                                            <p>
                                                (b) Kewajiban adalah utang yang timbul dari peristiwa masa lalu yang
                                                penyelesaiannya mengakibatkan aliran keluar sumber daya ekonomi
                                                pemerintah
                                                daerah.
                                            </p>
                                            <p>
                                                (c) Ekuitas adalah kekayaan bersih pemerintah daerah yang merupakan
                                                selisih
                                                antara aset dan kewajiban pemerintah daerah.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step" class="section-block"><strong class="block-title">Aset</strong>
                                            </p>
                                            <p>
                                                39. Manfaat ekonomi masa depan yang terwujud dalam aset adalah potensi
                                                aset
                                                tersebut untuk memberikan sumbangan, baik langsung maupun tidak
                                                langsung,
                                                bagi kegiatan operasional pemerintah daerah, berupa aliran pendapatan
                                                atau
                                                penghematan belanja bagi pemerintah daerah.
                                            </p>
                                            <p>
                                                40. Aset diklasifikasikan ke dalam aset lancar dan nonlancar. Suatu aset
                                                diklasifikasikan sebagai aset lancar jika diharapkan segera untuk dapat
                                                direalisasikan atau dimiliki untuk dipakai atau dijual dalam waktu 12
                                                (dua
                                                belas) bulan sejak tanggal pelaporan. Aset yang tidak dapat dimasukkan
                                                dalam kriteria tersebut diklasifikasikan sebagai aset nonlancar.
                                            </p>
                                            <p>
                                                41. Aset lancar meliputi kas dan setara kas, investasi jangka pendek,
                                                piutang, dan persediaan.
                                            </p>
                                            <p>
                                                42. Aset nonlancar mencakup aset yang bersifat jangka panjang, dan aset
                                                tak
                                                berwujud yang digunakan baik langsung maupun tidak langsung untuk
                                                kegiatan
                                                pemerintah daerah atau yang digunakan masyarakat umum. Aset nonlancar
                                                diklasifikasikan menjadi investasi jangka panjang, aset tetap, dana
                                                cadangan, dan aset lainnya.
                                            </p>
                                            <p>
                                                43. Investasi jangka panjang merupakan investasi yang diadakan dengan
                                                maksud untuk mendapatkan manfaat ekonomi dan manfaat sosial dalam jangka
                                                waktu lebih dari satu periode akuntansi. Investasi jangka panjang
                                                meliputi
                                                investasi nonpermanen dan permanen. Investasi nonpermanen antara lain
                                                dana
                                                bergulir, penyertaan modal dalam proyek pembangunan, dan investasi
                                                nonpermanen lainnya. Investasi permanen antara lain penyertaan modal
                                                pemerintah daerah dan investasi permanen lainnya.
                                            </p>
                                            <p>
                                                44. Aset tetap meliputi tanah, peralatan dan mesin, gedung dan bangunan,
                                                jalan, irigasi, dan jaringan, aset tetap lainnya, dan konstruksi dalam
                                                pengerjaan.
                                            </p>
                                            <p>
                                                45. Aset nonlancar lainnya diklasifikasikan sebagai aset lainnya.
                                                Termasuk
                                                dalam aset lainnya adalah aset tak berwujud dan aset kerja sama
                                                (kemitraan).
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step" class="section-block"><strong class="block-title">Kewajiban
                                            </strong>
                                            </p>
                                            <p>
                                                46. Karakteristik esensial kewajiban adalah bahwa pemerintah daerah
                                                mempunyai kewajiban masa kini yang dalam penyelesaiannya mengakibatkan
                                                pengorbanan sumber daya ekonomi di masa yang akan datang.
                                            </p>
                                            <p>
                                                47. Kewajiban umumnya timbul karena konsekuensi pelaksanaan tugas atau
                                                tanggung jawab untuk bertindak di masa lalu. Dalam konteks pemerintahan,
                                                kewajiban muncul antara lain karena penggunaan sumber pembiayaan
                                                pinjaman
                                                dari masyarakat, lembaga keuangan, entitas pemerintah daerah lain, atau
                                                lembaga internasional. Kewajiban pemerintah daerah juga terjadi karena
                                                perikatan dengan pegawai yang bekerja pada pemerintah daerah atau dengan
                                                pemberi jasa lainnya.
                                            </p>
                                            <p>
                                                48. Setiap kewajiban dapat dipaksakan menurut hukum sebagai konsekuensi
                                                dari kontrak yang mengikat atau peraturan perundang-undangan.
                                            </p>
                                            <p>
                                                49. Kewajiban dikelompokkan ke dalam kewajiban jangka pendek dan
                                                kewajiban
                                                jangka panjang. Kewajiban jangka pendek merupakan kelompok kewajiban
                                                yang
                                                diselesaikan dalam waktu kurang dari dua belas bulan setelah tanggal
                                                pelaporan. Kewajiban jangka panjang adalah kelompok kewajiban yang
                                                penyelesaiannya dilakukan setelah 12 (dua belas) bulan sejak tanggal
                                                pelaporan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step" class="section-block"><strong class="block-title">Ekuitas
                                            </strong>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step" class="section-block"><strong class="block-title"></strong>

                                            50. Ekuitas adalah kekayaan bersih pemerintah daerah yang merupakan
                                            selisih
                                            antara aset dan kewajiban pemerintah daerah pada tanggal laporan. Saldo
                                            ekuitas di Neraca berasal dari saldo akhir ekuitas pada Laporan
                                            Perubahan
                                            Ekuitas.

                                        </div>
                                    </li>
                                    <li>
                                        <div id="step27" class="section-block"><strong class="block-title">Laporan
                                                Operasional</strong>


                                        </div>
                                    </li>
                                    <li>
                                        <div id="step" class="section-block"><strong class="block-title">51. Laporan
                                                Operasional</strong>
                                            menyajikan ikhtisar sumber daya
                                            ekonomi yang menambah ekuitas dan penggunaannya yang dikelola oleh
                                            pemerintah daerah untuk kegiatan penyelenggaraan pemerintahan dalam satu
                                            pelaporan.
                                            </p>
                                            <p>
                                                52. Unsur yang dicakup secara langsung dalam Laporan Operasional terdiri
                                                dari pendapatan-LO, beban, transfer, dan pos-pos luar biasa.
                                                Masing-masing
                                                unsur dapat dijelaskan sebagai berikut :
                                            </p>
                                            <p>
                                                a) Pendapatan-LO adalah hak pemerintah daerah yang diakui sebagai
                                                penambah
                                                nilai kekayaan bersih.
                                            </p>
                                            <p>
                                                b) Beban adalah kewajiban pemerintah daerah yang diakui sebagai
                                                pengurang
                                                nilai kekayaan bersih.
                                            </p>
                                            <p>
                                                c) Transfer adalah hak penerimaan atau kewajiban pengeluaran uang
                                                dari/oleh
                                                suatu entitas pelaporan dari/kepada entitas pelaporan lain, termasuk
                                                dana
                                                perimbangan dan dana bagi hasil.
                                            </p>
                                            <p>
                                                d) Pos Luar Biasa adalah pendapatan luar biasa atau beban luiar biasa
                                                yang
                                                terjadi karena kejadian atau transaksi yang bukan merupakan operasi
                                                biasa,
                                                tidak diharapkan sering atau rutin terjadi, dan berada di luar kendali
                                                atau
                                                pengaruh entitas bersangkutan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step28" class="section-block"><strong class="block-title">Laporan Arus
                                                Kas</strong>

                                        </div>
                                    </li>
                                    <li>
                                        <div id="step" class="section-block"><strong class="block-title">53. Laporan
                                                Arus
                                                Kas</strong>
                                            merupakan laporan yang menyajikan
                                            informasi kas sehubungan dengan aktivitas operasi, investasi, pendanaan,
                                            dan transitoris yang menggambarkan saldo awal, penerimaan, pengeluaran, dan
                                            saldo akhir kas pemerintah daerah selama periode tertentu.
                                            </p>
                                            <p>
                                                54. Unsur yang dicakup dalam Laporan Arus Kas terdiri dari penerimaan
                                                dan
                                                pengeluaran kas, yang masing-masing didefinisikan sebagai berikut:
                                            </p>
                                            <p>
                                                (a) Penerimaan kas adalah semua aliran kas yang masuk ke Bendahara Umum
                                                Daerah.
                                            </p>
                                            <p>
                                                (b) Pengeluaran kas adalah semua aliran kas yang keluar dari Bendahara
                                                Umum
                                                Daerah.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step29" class="section-block"><strong class="block-title">Laporan
                                                Perubahan
                                                Ekuitas</strong>

                                        </div>
                                    </li>
                                    <li>
                                        <div id="step" class="section-block"><strong class="block-title">55. Laporan
                                                Perubahan
                                                Ekuitas
                                            </strong>menyajikan informasi
                                            kenaikan atau penurunan ekuitas tahun pelaporan dibandingkan dengan tahun
                                            sebelumnya.

                                        </div>
                                    </li>

                                    <li>
                                        <div id="step30" class="section-block"><strong class="block-title">Catatan atas
                                                Laporan
                                                Keuangan</strong>

                                        </div>
                                    </li>
                                    <li>
                                        <div id="step30" class="section-block"><strong class="block-title">56. Catatan
                                                Atas
                                                Laporan
                                                Keuangan</strong> menyajikan penjelasan
                                            naratif atau rincian dari angka yang tertera dalam Laporan Realisasi
                                            Anggaran, Laporan Perubahan SAL, Laporan Operasional, Laporan Perubahan
                                            Ekuitas, Neraca, dan Laporan Arus Kas. Catatan atas Laporan Keuangan juga
                                            mencakup informasi tentang kebijakan akuntansi yang dipergunakan oleh
                                            pemerintah daerah dan informasi lain yang diharuskan dan dianjurkan untuk
                                            diungkapkan di dalam Standar Akuntansi Pemerintahan serta ungkapan-ungkapan
                                            yang diperlukan untuk menghasilkan penyajian laporan keuangan secara wajar.
                                            Catatan atas Laporan Keuangan mengungkapkan hal-hal sebagai berikut:
                                            </p>
                                            <p>
                                                (a) Menyajikan informasi tentang kebijakan fiskal/keuangan, ekonomi
                                                regional/ekonomi makro, pencapaian target peraturan daerah APBD, berikut
                                                kendala dan hambatan yang dihadapi dalam pencapaian target;
                                            </p>
                                            <p>
                                                (b) Menyajikan ikhtisar pencapaian kinerja keuangan selama tahun
                                                pelaporan;
                                            </p>
                                            <p>
                                                (c) Menyajikan informasi tentang dasar penyusunan laporan keuangan dan
                                                kebijakan-kebijakan akuntansi yang dipilih untuk diterapkan atas
                                                transaksi-transaksi dan kejadian-kejadian penting lainnya;
                                            </p>
                                            <p>
                                                (d) Menyajikan rincian dan penjelasan masing-masing pos yang disajikan
                                                pada
                                                lembar muka laporan keuangan;
                                            </p>
                                            <p>
                                                (e) Menyediakan informasi tambahan yang diperlukan untuk penyajian yang
                                                wajar, yang tidak disajikan dalam lembar muka (<em>on the face</em>)
                                                laporan keuangan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step31" class="section-block"><strong class="block-title">G. PENGAKUAN
                                                UNSUR LAPORAN
                                                KEUANGAN</strong>

                                            </p>
                                            <p>
                                                57. Pengakuan dalam akuntansi adalah proses penetapan terpenuhinya
                                                kriteria
                                                pencatatan suatu kejadian atau peristiwa dalam catatan akuntansi
                                                sehingga
                                                akan menjadi bagian yang melengkapi unsur aset, kewajiban, ekuitas,
                                                pendapatan-LRA, belanja, pembiayaan, pendapatan-LO, dan beban
                                                sebagaimana
                                                akan termuat pada laporan keuangan Pemerintah Daerah. Pengakuan
                                                diwujudkan
                                                dalam pencatatan jumlah uang terhadap pos-pos laporan keuangan yang
                                                terpengaruh oleh kejadian atau peristiwa terkait.
                                            </p>
                                            <p>
                                                58. Kriteria minimum yang perlu dipenuhi oleh suatu kejadian atau
                                                peristiwa
                                                untuk diakui yaitu:
                                            </p>
                                            <p>
                                                a. terdapat kemungkinan bahwa manfaat ekonomi yang berkaitan dengan
                                                kejadian atau peristiwa tersebut akan mengalir keluar dari atau masuk ke
                                                dalam Pemerintah Daerah;
                                            </p>
                                            <p>
                                                b. kejadian atau peristiwa tersebut mempunyai nilai atau biaya yang
                                                dapat
                                                diukur atau dapat diestimasi dengan andal.
                                            </p>
                                            <p>
                                                59. Dalam menentukan apakah suatu kejadian/peristiwa memenuhi kriteria
                                                pengakuan, perlu mempertimbangkan aspek materialitas.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step32" class="section-block"><strong class="block-title">Kemungkinan
                                                Besar
                                                Manfaat
                                                Ekonomi Masa Depan Terjadi</strong>
                                            </p>
                                            <p>
                                                60. Dalam kriteria pengakuan pendapatan, konsep kemungkinan besar
                                                manfaat
                                                ekonomi masa depan terjadi digunakan dalam pengertian derajat kepastian
                                                tinggi bahwa manfaat ekonomi masa depan yang berkaitan dengan pos atau
                                                kejadian/peristiwa tersebut akan mengalir dari atau ke pemerintah
                                                daerah.
                                                Konsep ini diperlukan dalam menghadapi ketidakpastian lingkungan
                                                operasional pemerintah daerah. Pengkajian derajat kepastian yang melekat
                                                dalam arus manfaat ekonomi masa depan dilakukan atas dasar bukti yang
                                                dapat
                                                diperoleh pada saat penyusunan laporan keuangan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step33" class="section-block"><strong class="block-title">Keandalan
                                                Pengukuran</strong>
                                            </p>
                                            <p>
                                                61. Kriteria pengakuan pada umumnya didasarkan pada nilai uang akibat
                                                peristiwa atau kejadian yang dapat diandalkan pengukurannya. Namun ada
                                                kalanya pengakuan didasarkan pada hasil estimasi yang layak. Apabila
                                                pengukuran berdasarkan biaya dan estimasi yang layak tidak mungkin
                                                dilakukan, maka pengakuan transaksi demikian cukup diungkapkan pada
                                                Catatan
                                                atas Laporan Keuangan.
                                            </p>
                                            <p>
                                                62. Penundaan pengakuan suatu pos atau peristiwa dapat terjadi apabila
                                                kriteria pengakuan baru terpenuhi setelah terjadi atau tidak terjadi
                                                peristiwa atau keadaan lain di masa mendatang.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step34" class="section-block"><strong class="block-title">Pengakuan
                                                Aset</strong>
                                            </p>
                                            <p>
                                                63. Aset diakui pada saat potensi manfaat ekonomi masa depan diperoleh
                                                oleh
                                                pemerintah daerah dan mempunyai nilai atau biaya yang dapat diukur
                                                dengan
                                                andal.
                                            </p>
                                            <p>
                                                64. Sejalan dengan penerapan basis akrual, aset dalam bentuk piutang
                                                atas
                                                beban dibayar dimuka diakui ketika hak klaim untuk mendapatkan arus kas
                                                masuk atau manfaat ekonomi lainnya dari entitas lain telah atau tetap
                                                masih
                                                terpenuhi, dan dinilai klaim tersebut dapat diukur atau diestimasi.
                                            </p>
                                            <p>
                                                65. Aset dalam bentuk kas yang diperoleh pemerintah daerah antara lain
                                                bersumber dari pajak, retribusi, pungutan hasil pemanfaatan kekayaan
                                                daerah, transfer, dan setoran lain-lain, serta penerimaan pembiayaan,
                                                seperti hasil pinjaman. Proses pemungutan setiap unsur penerimaan
                                                tersebut
                                                sangat beragam dan melibatkan banyak pihak atau instansi. Dengan
                                                demikian,
                                                titik pengakuan penerimaan kas oleh pemerintah daerah untuk mendapatkan
                                                pengakuan akuntansi memerlukan pengaturan yang lebih rinci, termasuk
                                                pengaturan mengenai batasan waktu sejak uang diterima sampai
                                                penyetorannya
                                                ke Rekening Kas Umum Daerah. Aset tidak diakui jika pengeluaran telah
                                                terjadi dan manfaat ekonominya dipandang tidak mungkin diperoleh
                                                pemerintah
                                                daerah setelah periode akuntansi berjalan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step34" class="section-block"><strong class="block-title"></strong>
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step35" class="section-block"><strong class="block-title">Pengakuan
                                                Kewajiban</strong>
                                            </p>
                                            <p>
                                                66. Kewajiban diakui jika besar kemungkinan bahwa pengeluaran sumber
                                                daya
                                                ekonomi akan dilakukan untuk menyelesaikan kewajiban yang ada sampai
                                                saat
                                                pelaporan, dan perubahan atas kewajiban tersebut mempunyai nilai
                                                penyelesaian yang dapat diukur dengan andal.
                                            </p>
                                            <p>
                                                67. Sejalan dengan penerapan basis akrual, kewajiban diakui pada saat
                                                dana
                                                pinjaman diterima atau pada saat kewajiban timbul.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step36" class="section-block"><strong class="block-title">Pengakuan
                                                Pendapatan</strong>
                                            </p>
                                            <p>
                                                68. Pendapatan LRA diakui pada saat diterima di Rekening Kas Umum Daerah
                                                atau oleh entitas pelaporan. Pendapatan LO diakui pada saat timbulnya
                                                hak
                                                atas pendapatan tersebut.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="37" class="section-block"><strong class="block-title">Pengakuan Beban
                                                dan
                                                Belanja</strong>
                                            </p>
                                            <p>
                                                69. Belanja diakui pada saat terjadinya pengeluaran dari Rekening Kas
                                                Umum
                                                Daerah atau entitas pelaporan. Khusus pengeluaran melalui bendahara
                                                pengeluaran pengakuannya terjadi pada saat pertanggungjawaban atas
                                                pengeluaran tersebut disahkan oleh unit yang mempunyai fungsi
                                                perbendaharaan. Beban diakui pada saat timbulnya kewajiban, terjadinya
                                                konsumsi aset, atau terjadinya penurunan manfaat ekonomi atau potensi
                                                jasa.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step38" class="section-block"><strong class="block-title">H. PENGUKURAN
                                                UNSUR LAPORAN
                                                KEUANGAN</strong>
                                            </p>
                                            <p>
                                                70. Pengukuran adalah proses penetapan nilai uang untuk mengakui dan
                                                memasukkan setiap pos dalam laporan keuangan Pemerintah Daerah.
                                                Pengukuran
                                                pos-pos dalam laporan keuangan Pemerintah Daerah menggunakan nilai
                                                perolehan historis. Aset dicatat sebesar pengeluaran/penggunaan sumber
                                                daya
                                                ekonomi atau sebesar nilai wajar dari imbalan yang diberikan untuk
                                                memperoleh aset tersebut. Kewajiban dicatat sebesar nilai wajar sumber
                                                daya
                                                ekonomi yang digunakan pemerintah daerah untuk memenuhi kewajiban yang
                                                bersangkutan.
                                            </p>
                                            <p>
                                                71. Pengukuran pos-pos laporan keuangan menggunakan mata uang Rupiah.
                                                Transaksi yang menggunakan mata uang asing harus dikonversikan terlebih
                                                dahulu (menggunakan kurs tengah Bank Indonesia) dan dinyatakan dalam
                                                mata
                                                uang rupiah.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step39" class="section-block"><strong class="block-title">I. ASUMSI
                                                DASAR</strong>
                                            </p>
                                            <p>
                                                72. Asumsi dasar dalam pelaporan keuangan Pemerintah Daerah adalah
                                                anggapan
                                                yang diterima sebagai suatu kebenaran tanpa perlu dibuktikan agar
                                                kebijakan
                                                akuntansi dapat diterapkan, yang terdiri atas:
                                            </p>
                                            <p>
                                                a. asumsi kemandirian entitas;
                                            </p>
                                            <p>
                                                b. asumsi kesinambungan entitas; dan
                                            </p>
                                            <p>
                                                c. asumsi keterukuran dalam satuan uang (<em>monetary measurement</em>)
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step40" class="section-block"><strong class="block-title">Kemandirian
                                                Entitas</strong>
                                            </p>
                                            <p>
                                                73. Asumsi kemandirian entitas, yang berarti bahwa unit Pemerintah
                                                Daerah
                                                sebagai entitas pelaporan dan entitas akuntansi dianggap sebagai unit
                                                yang
                                                mandiri dan mempunyai kewajiban untuk menyajikan laporan keuangan
                                                sehingga
                                                tidak terjadi kekacauan antar unit pemerintahan dalam pelaporan
                                                keuangan.
                                                Salah satu indikasi terpenuhinya asumsi ini adalah adanya kewenangan
                                                entitas untuk menyusun anggaran dan melaksanakannya dengan tanggung
                                                jawab
                                                penuh. Entitas bertanggung jawab atas pengelolaan aset dan sumber daya
                                                di
                                                luar neraca untuk kepentingan yurisdiksi tugas pokoknya, termasuk atas
                                                kehilangan atau kerusakan aset dan sumber daya dimaksud, utang piutang
                                                yang
                                                terjadi akibat pembuatan keputusan entitas, serta terlaksana tidaknya
                                                program dan kegiatan yang telah ditetapkan.
                                            </p>
                                            <p>
                                                74. Entitas di pemerintah daerah terdiri atas Entitas Pelaporan dan
                                                Entitas
                                                Akuntansi.
                                            </p>
                                            <p>
                                                75. Entitas Pelaporan adalah Pemerintah Daerah yang terdiri dari satu
                                                atau
                                                lebih entitas akuntansi yang menurut ketentuan peraturan
                                                perundang-undangan
                                                wajib menyampaikan laporan pertanggung-jawaban berupa laporan keuangan
                                                Pemda.
                                            </p>
                                            <p>
                                                76. Entitas Akuntansi adalah Satuan Kerja penguna anggaran/pengguna
                                                barang
                                                dan oleh karenanya wajib menyelenggarakan akuntansi dan menyusun laporan
                                                keuangan untuk digabungkan pada entitas pelaporan. Yang termasuk ke
                                                dalam
                                                entitas akuntansi adalah Perangkat Daerah dan PPKD.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step41" class="section-block"><strong class="block-title">Kesinambungan
                                                Entitas</strong>
                                            </p>
                                            <p>
                                                77. Laporan keuangan Pemerintah Daerah disusun dengan asumsi bahwa
                                                Pemerintah Daerah akan berlanjut keberadaannya dan tidak bermaksud untuk
                                                melakukan likuidasi.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step42" class="section-block"><strong class="block-title">Keterukuran
                                                dalam
                                                Satuan Uang
                                                (<em>Monetary Measurement</em>)</strong>
                                            </p>
                                            <p>
                                                78. Laporan keuangan Pemerintah Daerah harus menyajikan setiap kegiatan
                                                yang diasumsikan dapat dinilai dengan satuan uang. Hal ini diperlukan
                                                agar
                                                memungkinkan dilakukannya analisis dan pengukuran dalam akuntansi.
                                            </p>
                                            <p>
                                        </div>
                                    </li>

                                    <li>
                                        <div id="step43" class="section-block"><strong class="block-title">J. PRINSIP
                                                AKUNTANSI DAN
                                                PELAPORAN KEUANGAN</strong>
                                            </p>
                                            <p>
                                                79. Prinsip akuntansi dan pelaporan keuangan dimaksudkan sebagai
                                                ketentuan
                                                yang harus dipahami dan ditaati oleh penyelenggara akuntansi dan
                                                pelaporan
                                                keuangan Pemerintah Daerah dalam melakukan kegiatannya, serta oleh
                                                pengguna
                                                laporan dalam memahami laporan keuangan yang disajikan. Berikut ini
                                                adalah
                                                delapan prinsip yang digunakan dalam akuntansi dan pelaporan keuangan
                                                Pemerintah Daerah:
                                            </p>
                                            <p>
                                                a) basis akuntansi;
                                            </p>
                                            <p>
                                                b) prinsip nilai historis;
                                            </p>
                                            <p>
                                                c) prinsip realisasi;
                                            </p>
                                            <p>
                                                d) prinsip substansi mengungguli formal;
                                            </p>
                                            <p>
                                                e) prinsip periodisitas;
                                            </p>
                                            <p>
                                                f) prinsip konsistensi;
                                            </p>
                                            <p>
                                                g) prinsip pengungkapan lengkap; dan
                                            </p>
                                            <p>
                                                h) prinsip penyajian wajar.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step44" class="section-block"><strong class="block-title">Basis
                                                Akuntansi</strong>
                                            </p>
                                            <p>
                                                80. Basis akuntansi yang digunakan dalam laporan keuangan Pemerintah
                                                Daerah
                                                adalah basis akrual, untuk pengakuan pendapatan-LO, beban, aset,
                                                kewajiban,
                                                dan ekuitas. Dalam hal peraturan perundangan mewajibkan disajikan
                                                laporan
                                                keuangan dengan basis kas, maka entitas wajib menyajikan laporan
                                                demikian.
                                            </p>
                                            <p>
                                                81. Basis akrual untuk LO berarti bahwa pendapatan diakui pada saat hak
                                                untuk memperoleh pendapatan telah terpenuhi walaupun kas belum diterima
                                                di
                                                Rekening Kas Umum Daerah atau oleh entitas pelaporan. Beban diakui pada
                                                saat kewajiban yang mengakibatkan penurunan nilai kekayaan bersih telah
                                                terpenuhi walaupun kas belum dikeluarkan dari Rekening Kas Umum Daerah
                                                atau
                                                entitas pelaporan. Pendapatan seperti bantuan pihak luar/asing dalam
                                                bentuk
                                                jasa disajikan pula pada LO.
                                            </p>
                                            <p>
                                                82. Dalam hal anggaran disusun dan dilaksanakan berdasarkan basis kas,
                                                maka
                                                LRA disusun berdasarkan basis kas, berarti bahwa pendapatan dan
                                                penerimaan
                                                pembiayaan diakui pada saat kas diterima di Rekening Kas Umum Daerah
                                                atau
                                                oleh entitas pelaporan; serta belanja, transfer, dan pengeluaran
                                                pembiayaan
                                                diakui pada saat kas dikeluarkan dari Rekening Kas Umum Daerah. Namun
                                                demikian, bilamana anggaran disusun dan dilaksanakan berdasarkan basis
                                                akrual, maka LRA disusun berdasarkan basis akrual.
                                            </p>
                                            <p>
                                                83. Basis akrual untuk Neraca berarti bahwa aset, kewajiban, dan ekuitas
                                                diakui dan dicatat pada saat terjadinya transaksi, atau pada saat
                                                kejadian
                                                atau kondisi lingkungan berpengaruh pada keuangan pemerintah daerah,
                                                tanpa
                                                memperhatikan saat kas atau setara kas diterima atau dibayar.
                                            </p>
                                            <p>
                                        </div>
                                    </li>

                                    <li>
                                        <div id="step45" class="section-block"><strong class="block-title">Prinsip Nilai
                                                Historis <em>(Historical
                                                    Cost Principle)</em></strong>
                                            </p>
                                            <p>
                                                84. Aset dicatat sebesar pengeluaran kas dan setara kas yang dibayar
                                                atau
                                                sebesar nilai wajar dari imbalan (<em>consideration</em>) untuk
                                                memperoleh
                                                aset tersebut pada saat perolehan. Kewajiban dicatat sebesar jumlah kas
                                                dan
                                                setara yang diharapkan akan dibayarkan untuk memenuhi kewajiban di masa
                                                yang akan datang dalam pelaksanaan kegiatan Pemerintah Daerah.
                                            </p>
                                            <p>
                                                85. Penggunaan nilai historis lebih dapat diandalkan daripada penilaian
                                                yang lain karena nilai perolehan lebih obyektif dan dapat diverifikasi.
                                                Dalam hal tidak terdapat nilai historis, dapat digunakan nilai wajar
                                                aset
                                                atau kewajiban terkait.
                                            </p>
                                            <p>
                                        </div>
                                    </li>

                                    <li>
                                        <div id="step46" class="section-block"><strong class="block-title">Prinsip
                                                Realisasi
                                                (<em>Realization Principle</em>)</strong>
                                            </p>
                                            <p>
                                                86. Bagi pemerintah daerah, pendapatan basis kas yang tersedia yang
                                                telah
                                                diotorisasikan melalui anggaran pemerintah daerah suatu periode
                                                akuntansi
                                                akan digunakan untuk membayar utang dan belanja dalam periode tersebut.
                                                Mengingat LRA masih merupakan laporan yang wajib disusun, maka
                                                pendapatan
                                                atau belanja basis kas diakui setelah otorisasi melalui anggaran dan
                                                telah
                                                menambah atau mengurangi kas.
                                            </p>
                                            <p>
                                                87. Prinsip layak temu biaya-pendapatan ( <em>matching cost against
                                                    revenue
                                                    principle</em>) tidak ditekankan dalam
                                                akuntansi pemerintah daerah, sebagaimana dipraktekkan dalam akuntansi
                                                sektor komersial.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step47" class="section-block"><strong class="block-title">Prinsip
                                                Substansi
                                                Mengungguli
                                                Bentuk
                                                Formal
                                                (<em>Substance Over
                                                    Form Principle</em>)</strong>
                                            </p>
                                            <p>
                                                88. Informasi akuntansi dimaksudkan untuk menyajikan dengan wajar
                                                transaksi
                                                serta peristiwa lain yang seharusnya disajikan, maka transaksi atau
                                                peristiwa lain tersebut harus dicatat dan disajikan sesuai dengan
                                                substansi
                                                dan realitas ekonomi, bukan hanya mengikuti aspek formalitasnya. Apabila
                                                substansi transaksi atau peristiwa lain tidak konsisten/berbeda dengan
                                                aspek formalitasnya, maka hal tersebut harus diungkapkan dengan jelas
                                                dalam
                                                Catatan Atas Laporan Keuangan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step48" class="section-block"><strong class="block-title">Prinsip
                                                Periodisitas
                                                (<em>Periodicity Principle</em>)</strong>
                                            </p>
                                            <p>
                                                89. Kegiatan akuntansi dan pelaporan keuangan Pemerintah Daerah perlu
                                                dibagi menjadi periode-periode pelaporan sehingga kinerja Pemerintah
                                                Daerah
                                                dapat diukur dan posisi sumber daya yang dimilikinya dapat ditentukan.
                                            </p>
                                            <p>
                                                Periode utama pelaporan keuangan yang digunakan adalah tahunan. Namun
                                                untuk
                                                laporan realisasi anggaran dibuat periode semester.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step49" class="section-block"><strong class="block-title">Prinsip
                                                Konsistensi
                                                (<em>Consistency Principle</em>)</strong>
                                            </p>
                                            <p>
                                                90. Perlakuan akuntansi yang sama harus diterapkan pada kejadian yang
                                                serupa dari periode ke periode oleh Pemerintah Daerah (prinsip
                                                konsistensi
                                                internal). Hal ini tidak berarti bahwa tidak boleh terjadi perubahan
                                                dari
                                                satu metode akuntansi ke metode akuntansi yang lain.
                                            </p>
                                            <p>
                                                91. Metode akuntansi yang dipakai dapat diubah dengan syarat bahwa
                                                metode
                                                yang baru diterapkan harus menunjukkan hasil yang lebih baik dari metode
                                                yang lama. Pengaruh dan pertimbangan atas perubahan penerapan metode ini
                                                harus diungkapkan dalam Catatan Atas Laporan Keuangan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step50" class="section-block"><strong class="block-title">Prinsip
                                                Pengungkapan Lengkap
                                                (<em>Full Disclosure Principle</em>)
                                            </strong>
                                            </p>
                                            <p>
                                                92. Laporan keuangan Pemerintah Daerah harus menyajikan secara lengkap
                                                informasi yang dibutuhkan oleh pengguna laporan. Informasi yang
                                                dibutuhkan
                                                oleh pengguna laporan dapat ditempatkan pada lembar muka ( <em>on the
                                                    face</em>) laporan
                                                keuangan atau Catatan atas laporan keuangan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step51" class="section-block"><strong class="block-title">Prinsip
                                                Penyajian
                                                Wajar
                                                (<em>Fair Presentation Principle</em>)
                                            </strong>
                                            </p>
                                            <p>
                                                93. Laporan keuangan Pemerintah Daerah harus menyajikan dengan wajar
                                                Laporan Realisasi Anggaran, Laporan Perubahan Saldo Anggaran Lebih,
                                                Neraca,
                                                Laporan Operasional, Laporan Arus Kas, Laporan Perubahan Ekuitas dan
                                                Catatan Atas Laporan Keuangan.
                                            </p>
                                            <p>
                                                94. Faktor pertimbangan sehat bagi penyusun laporan keuangan Pemerintah
                                                Daerah diperlukan ketika menghadapi ketidakpastian peristiwa dan keadaan
                                                tertentu. Ketidakpastian seperti itu diakui dengan mengungkapkan hakikat
                                                serta tingkatnya dengan menggunakan pertimbangan sehat dalam penyusunan
                                                laporan keuangan Pemerintah Daerah. Pertimbangan sehat mengandung unsur
                                                kehati-hatian pada saat melakukan prakiraan dalam kondisi ketidakpastian
                                                sehingga aset atau pendapatan tidak dinyatakan terlalu tinggi serta
                                                kewajiban dan belanja tidak dinyatakan terlalu rendah. Namun demikian,
                                                penggunaan pertimbangan sehat tidak memperkenankan, misalnya pembentukan
                                                dana cadangan tersembunyi, sengaja menetapkan aset atau pendapatan yang
                                                terlampau rendah atau sengaja mencatat kewajiban dan belanja yang
                                                terlampau
                                                tinggi, sehingga laporan keuangan tidak netral dan tidak andal.
                                            </p>
                                            <p>
                                        </div>
                                    </li>

                                    <li>
                                        <div id="step52" class="section-block"><strong class="block-title">K. KENDALA
                                                INFORMASI
                                                AKUNTANSI YANG RELEVAN DAN ANDAL</strong>
                                            </p>
                                            <p>
                                                95. Kendala informasi yang relevan dan andal adalah setiap keadaan yang
                                                tidak memungkinkan tercapainya kondisi ideal dalam mewujudkan informasi
                                                akuntansi yang relevan dan andal dalam laporan keuangan Pemerintah
                                                Daerah
                                                sebagai akibat keterbatasan (<em>limitations</em>) atau karena
                                                alasan-alasan tertentu. Tiga hal yang mengakibatkan kendala dalam
                                                mewujudkan informasi akuntansi yang relevan dan andal, yaitu:
                                            </p>
                                            <p>
                                                a. Materialitas;
                                            </p>
                                            <p>
                                                b. Pertimbangan biaya dan manfaat; dan
                                            </p>
                                            <p>
                                                c. Keseimbangan antar karakteristik kualitatif.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step53" class="section-block"><strong
                                                class="block-title">Materialitas</strong>
                                            </p>
                                            <p>
                                                96. Laporan keuangan Pemerintah Daerah walaupun idealnya memuat segala
                                                informasi, tetapi hanya diharuskan memuat informasi yang memenuhi
                                                kriteria
                                                materialitas. Informasi dipandang material apabila kelalaian untuk
                                                mencantumkan atau kesalahan dalam mencatat informasi tersebut dapat
                                                mempengaruhi keputusan pengguna laporan yang dibuat atas dasar informasi
                                                dalam laporan keuangan Pemerintah Daerah.
                                            </p>
                                            <p>
                                        </div>
                                    </li>

                                    <li>
                                        <div id="step54" class="section-block"><strong class="block-title">Pertimbangan
                                                Biaya dan
                                                Manfaat</strong>
                                            </p>
                                            <p>
                                                97. Manfaat yang dihasilkan dari informasi yang dimuat dalam laporan
                                                keuangan Pemerintah Daerah seharusnya melebihi dari biaya yang
                                                diperlukan
                                                untuk penyusunan laporan tersebut. Oleh karena itu, laporan keuangan
                                                Pemerintah Daerah tidak semestinya menyajikan informasi yang manfaatnya
                                                lebih kecil dibandingkan biaya penyusunannya. Namun demikian, evaluasi
                                                biaya dan manfaat merupakan proses pertimbangan yang substansial. Biaya
                                                dimaksud juga tidak harus dipikul oleh pengguna informasi yang menikmati
                                                manfaat.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step55" class="section-block"><strong class="block-title">Keseimbangan
                                                antar
                                                Karakteristik Kualitatif</strong>
                                            </p>
                                            <p>
                                                98. Keseimbangan antar karakteristik kualitatif diperlukan untuk
                                                mencapai
                                                suatu keseimbangan yang tepat di antara berbagai tujuan normatif yang
                                                diharapkan dipenuhi oleh laporan keuangan Pemerintah Daerah. Kepentingan
                                                relatif antar karakteristik kualitatif dalam berbagai kasus berbeda,
                                                terutama antara relevansi dan keandalan. Penentuan tingkat kepentingan
                                                antara dua karakteristik kualitatif tersebut merupakan masalah
                                                pertimbangan
                                                profesional.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step56" class="section-block"><strong class="block-title">L. DASAR
                                                HUKUM
                                                PELAPORAN
                                                KEUANGAN</strong>
                                            </p>
                                            <p>
                                                99. Pelaporan keuangan Pemerintah Daerah diselenggarakan berdasarkan
                                                peraturan perundang-undangan yang mengatur keuangan daerah, antara lain:
                                            </p>
                                            <p>
                                                a. Undang-undang Dasar Republik Indonesia 1945, khususnya bagian yang
                                                mengatur keuangan Negara; (khususnya pasal 23 ayat 1:
                                                <em>
                                                    Anggaran pendapatan dan belanja negara sebagai wujud dari
                                                    pengelolaan
                                                    keuangan negara ditetapkan setiap tahun dengan undang-undang dan
                                                    dilaksanakan secara terbuka dan bertanggung jawab untuk
                                                    sebesar-besarnya kemakmuran rakyat
                                                </em>
                                                .)
                                            </p>
                                            <p>
                                                b. Undang-undang No. 17 Tahun 2003 tentang Keuangan Negara;
                                            </p>
                                            <p>
                                                c. Undang-undang No. 1 Tahun 2003 tentang Perbendaharaan Negara;
                                            </p>
                                            <p>
                                                d. Undang-undang No. 15 Tahun 2004 tentang Pemeriksaan Pengelolaan dan
                                                Tanggung jawab Keuangan Negara;
                                            </p>
                                            <p>
                                                e. Undang-undang No. 33 Tahun 2004 tentang Perimbangan Keuangan antara
                                                Pemerintah Pusat dan Daerah;
                                            </p>
                                            <p>
                                                f. Undang-Undang Nomor 23 Tahun 2014 tentang Pemerintahan Daerah
                                                sebagaimana telah diubah beberapakali terakhir dengan Undang-Undang
                                                Nomor 9
                                                Tahun 2015 tentang Perubahan Kedua Atas Undang-Undang Nomor 23 Tahun
                                                2014
                                                tentang Pemerintahan Daerah;
                                            </p>
                                            <p>
                                                g. Peraturan Pemerintah No. 58 Tahun 2005 tentang Pengelolaan Keuangan
                                                Daerah;
                                            </p>
                                            <p>
                                                h. Peraturan Pemerintah No. 71 Tahun 2010 tentang Standar Akuntansi
                                                Pemerintahan;
                                            </p>
                                            <p>
                                                i. Peraturan Menteri Dalam Negeri Nomor 13 Tahun 2006 tentang Pedoman
                                                Pengelolaan Keuangan Daerah Sebagaimana telah beberapakali diubah
                                                terakhir
                                                dengan Peraturan Menteri Dalam Negeri Nomor 21 Tahun 2011 tentang
                                                Perubahan
                                                Kedua Atas Peraturan Menteri Dalam Negeri Nomor 13 Tahun 2006 tentang
                                                Pedoman Pengelolaan Keuangan Daerah;
                                            </p>
                                            <p>
                                                j. Peraturan Menteri Dalam Negeri No. 64 Tahun 2013 tentang Penerapan
                                                Standar Akuntansi Pemerintahan Berbasis Akrual Pada Pemerintah Daerah;
                                                dan
                                            </p>
                                            <p>
                                                k. Peraturan Daerah Nomor 2 Tahun 2013 tentang Pokok-pokok Pengelolaan
                                                Keuangan Daerah.
                                            </p>
                                        </div>
                                    </li>
