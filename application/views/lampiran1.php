                                                Politik</strong>
                                            </p>
                                            <p>
                                                15. Salah satu tujuan utama pemerintah daerah adalah meningkatkan
                                                kesejahteraan rakyat. Sehubungan dengan itu, pemerintah daerah berupaya
                                                untuk mewujudkan keseimbangan fiskal dengan mempertahankan kemampuan
                                                keuangan daerah yang bersumber dari pendapatan pajak dan sumber-sumber
                                                lainnya guna memenuhi keinginan masyarakat. Salah satu ciri yang penting
                                                dalam mewujudkan keseimbangan tersebut adalah berlangsungnya proses
                                                politik
                                                untuk menyelaraskan berbagai kepentingan yang ada di masyarakat.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step8" class="section-block">
                                            <strong class="block-title">
                                                Hubungan antara Pembayaran Pajak dan Pelayanan Pemerintah Daerah
                                            </strong>
                                            </p>
                                            <p>
                                                16. Walaupun dalam keadaan tertentu pemerintah daerah memungut secara
                                                langsung atas pelayanan yang diberikan, pada dasarnya sebagian besar
                                                pendapatan pemerintah daerah bersumber dari pungutan pajak dalam rangka
                                                memberikan pelayanan kepada masyarakat. Jumlah pajak yang dipungut tidak
                                                berhubungan langsung dengan pelayanan yang diberikan pemerintah daerah
                                                kepada wajib pajak. Pajak yang dipungut dan pelayanan yang diberikan
                                                oleh
                                                pemerintah daerah mengandung sifat-sifat tertentu yang wajib
                                                dipertimbangkan dalam mengembangkan laporan keuangan, antara lain
                                                sebagai
                                                berikut :
                                            </p>
                                            <p>
                                                (a) Pembayaran pajak bukan merupakan sumber pendapatan yang sifatnya
                                                suka
                                                rela.
                                            </p>
                                            <p>
                                                (b) Jumlah pajak yang dibayar ditentukan oleh basis pengenaan pajak
                                                sebagaimana ditentukan oleh peraturan perundang-undangan, seperti
                                                penghasilan yang diperoleh, kekayaan yang dimiliki, aktivitas bernilai
                                                tambah ekonomis, atau nilai kenikmatan yang diperoleh.
                                            </p>
                                            <p>
                                                (c) Efisiensi pelayanan yang diberikan pemerintah daerah dibandingkan
                                                dengan pungutan yang digunakan untuk pelayanan dimaksud sering sukar
                                                diukur
                                                sehubungan dengan monopoli pelayanan oleh pemerintah daerah. Dengan
                                                dibukanya kesempatan kepada pihak lain untuk menyelenggarakan pelayanan
                                                yang biasanya dilakukan pemerintah daerah, seperti layanan pendidikan
                                                dan
                                                kesehatan, pengukuran efisiensi pelayanan oleh pemerintah daerah menjadi
                                                lebih mudah.
                                            </p>
                                            <p>
                                                (d) Pengukuran kualitas dan kuantitas berbagai pelayanan yang diberikan
                                                pemerintah daerah adalah relatif sulit.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step9" class="section-block"><strong class="block-title">
                                                Anggaran sebagai Pernyataan Kebijakan Publik, Target Fiskal, dan Alat
                                                Pengendalian
                                            </strong>
                                            </p>
                                            <p>
                                                17. Anggaran pemerintah daerah merupakan dokumen formal hasil
                                                kesepakatan
                                                antara eksekutif dan legislatif tentang belanja yang ditetapkan untuk
                                                melaksanakan kegiatan pemerintah daerah dan pendapatan yang diharapkan
                                                untuk menutup keperluan belanja tersebut atau pembiayaan yang diperlukan
                                                bila diperkirakan akan terjadi defisit atau surplus. Dengan demikian,
                                                fungsi anggaran di lingkungan pemerintah daerah mempunyai pengaruh
                                                penting
                                                dalam akuntansi dan pelaporan keuangan, antara lain karena :
                                            </p>
                                            <p>
                                                (a) Anggaran merupakan pernyataan kebijakan publik.
                                            </p>
                                            <p>
                                                (b) Anggaran merupakan target fiskal yang menggambarkan keseimbangan
                                                antara
                                                belanja, pendapatan, dan pembiayaan yang diinginkan.
                                            </p>
                                            <p>
                                                (c) Anggaran menjadi landasan pengendalian yang memiliki konsekuensi
                                                hukum.
                                            </p>
                                            <p>
                                                (d) Anggaran memberi landasan penilaian kinerja pemerintah daerah.
                                            </p>
                                            <p>
                                                (e) Hasil pelaksanaan anggaran dituangkan dalam laporan keuangan
                                                pemerintah
                                                daerah sebagai pernyataan pertanggungjawaban pemerintah daerah kepada
                                                publik.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step10" class="section-block"><strong class="block-title">Investasi
                                                dalam
                                                Aset yang
                                                Tidak Langsung Menghasilkan Pendapatan</strong>
                                            </p>
                                            <p>
                                                18. Pemerintah daerah menginvestasikan dana yang besar dalam bentuk aset
                                                yang tidak secara langsung menghasilkan pendapatan bagi pemerintah
                                                daerah,
                                                seperti gedung perkantoran, jembatan, jalan, taman, dan kawasan
                                                reservasi.
                                                Sebagian besar aset dimaksud mempunyai masa manfaat yang lama sehingga
                                                program pemeliharaan dan rehabilitasi yang memadai diperlukan untuk
                                                mempertahankan manfaat yang hendak dicapai. Dengan demikian, fungsi aset
                                                dimaksud bagi pemerintah daerah berbeda dengan fungsinya bagi organisasi
                                                komersial. Sebagian besar aset tersebut tidak menghasilkan pendapatan
                                                secara langsung bagi pemerintah daerah, bahkan menimbulkan komitmen
                                                pemerintah daerah untuk memeliharanya di masa mendatang.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step11" class="section-block"><strong class="block-title">Peyusutan
                                                Aset
                                                Tetap</strong>
                                            </p>
                                            <p>
                                                19. Aset tetap yang digunakan pemerintah daerah, kecuali tanah dan
                                                konstruksi dalam pengerjaan, mempunyai masa manfaat dan kapasitas yang
                                                terbatas. Seiring dengan penurunan kapasitas dan manfaat dari suatu aset
                                                dilakukan penyesuaian nilai.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step12" class="section-block"><strong class="block-title">C. PERANAN
                                                DAN
                                                TUJUAN
                                                PELAPORAN KEUANGAN</strong>
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step13" class="section-block"><strong class="block-title">Peranan
                                                Laporan
                                                Keuangan</strong>
                                            </p>
                                            <p>
                                                20. Laporan keuangan Pemerintah Daerah disusun untuk menyediakan
                                                informasi
                                                yang relevan mengenai posisi keuangan dan seluruh transaksi yang
                                                dilakukan
                                                oleh Pemerintah Daerah selama satu periode pelaporan. Laporan keuangan
                                                Pemerintah Daerah terutama digunakan untuk mengetahui nilai sumber daya
                                                ekonomi yang dimanfaatkan untuk melaksanakan kegiatan operasional
                                                pemerintahan, menilai kondisi keuangan, menilai efektivitas dan
                                                efisiensi
                                                Pemerintah Daerah, dan membantu menentukan ketaatannya terhadap
                                                peraturan
                                                perundang-undangan.
                                            </p>
                                            <p>
                                                21. Pemerintah Daerah mempunyai kewajiban untuk melaporkan upaya-upaya
                                                yang
                                                telah dilakukan serta hasil yang dicapai dalam pelaksanaan kegiatan
                                                secara
                                                sistematis dan terstruktur pada suatu periode pelaporan untuk
                                                kepentingan:
                                            </p>
                                            <p>
                                                a. Akuntabilitas
                                            </p>
                                            <p>
                                                Mempertanggungjawabkan pengelolaan sumber daya serta pelaksanaan
                                                kebijakan
                                                yang dipercayakan kepada Pemerintah Daerah dalam mencapai tujuan yang
                                                telah
                                                ditetapkan secara periodik.
                                            </p>
                                            <p>
                                                b. Manajemen
                                            </p>
                                            <p>
                                                Membantu para pengguna laporan keuangan untuk mengevaluasi pelaksanaan
                                                kegiatan Pemerintah Daerah dalam periode pelaporan sehingga memudahkan
                                                fungsi perencanaan, pengelolaan dan pengendalian atas seluruh aset,
                                                kewajiban, dan ekuitas Pemerintah Daerah untuk kepentingan masyarakat.
                                            </p>
                                            <p>
                                                c. Transparansi
                                            </p>
                                            <p>
                                                Memberikan informasi keuangan yang terbuka dan jujur kepada masyarakat
                                                berdasarkan pertimbangan bahwa masyarakat memiliki hak untuk mengetahui
                                                secara terbuka dan menyeluruh atas pertanggungjawaban Pemerintah Daerah
                                                dalam pengelolaan sumber daya yang dipercayakan kepadanya dan
                                                ketaatannya
                                                pada peraturan perundang-undangan.
                                            </p>
                                            <p>
                                                d. Keseimbangan Antargenerasi (<em>Intergenerational equity</em>)
                                            </p>
                                            <p>
                                                Membantu para pengguna laporan untuk mengetahui apakah penerimaan
                                                Pemerintah Daerah pada periode laporan cukup untuk membiayai seluruh
                                                pengeluaran yang dialokasikan dan apakah generasi yang akan datang
                                                diasumsikan akan ikut menanggung beban pengeluaran tersebut.
                                            </p>
                                            <p>
                                                e. Evaluasi Kinerja
                                            </p>
                                            <p>
                                                Mengevalusi kinerja Pemerintah Daerah, terutama dalam penggunaan sumber
                                                daya ekonomi yang dikelola pemerintah daerah untuk mencapai kinerja yang
                                                direncanakan.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step14" class="section-block"><strong class="block-title">Tujuan
                                                Pelaporan
                                                Keuangan</strong>
                                            </p>
                                            <p>
                                                22. Pelaporan keuangan Pemerintah Daerah menyajikan informasi yang
                                                bermanfaat bagi para pengguna laporan dalam menilai akuntabilitas dan
                                                membuat keputusan baik keputusan ekonomi, sosial maupun politik dengan:
                                            </p>
                                            <p>
                                                a. menyediakan informasi tentang sumber, alokasi dan penggunaan sumber
                                                daya
                                                keuangan;
                                            </p>
                                            <p>
                                                b. menyediakan informasi mengenai kecukupan penerimaan periode berjalan
                                                untuk membiayai seluruh pengeluaran;
                                            </p>
                                            <p>
                                                c. menyediakan informasi mengenai jumlah sumber daya ekonomi yang
                                                digunakan
                                                dalam kegiatan Pemerintah Daerah serta hasil-hasil yang telah dicapai;
                                            </p>
                                            <p>
                                                d. menyediakan informasi mengenai bagaimana Pemerintah Daerah mendanai
                                                seluruh kegiatannya dan mencukupi kebutuhan kasnya;
                                            </p>
                                            <p>
                                                e. menyediakan informasi mengenai posisi keuangan dan kondisi Pemerintah
                                                Daerah berkaitan dengan sumber-sumber penerimaannya, baik jangka pendek
                                                maupun jangka panjang, termasuk yang berasal dari pungutan pajak dan
                                                pinjaman;
                                            </p>
                                            <p>
                                                f. menyediakan informasi mengenai perubahan posisi keuangan Pemerintah
                                                Daerah, apakah mengalami kenaikan atau penurunan, sebagai akibat
                                                kegiatan
                                                yang dilakukan selama periode pelaporan.
                                            </p>
                                            <p>
                                                23. Untuk memenuhi tujuan-tujuan tersebut, laporan keuangan Pemerintah
                                                Daerah menyediakan informasi mengenai sumber dan penggunaan sumber daya
                                                keuangan/ekonomi, transfer, pembiayaan, sisa lebih/kurang pelaksanaan
                                                anggaran, saldo anggaran lebih, surplus/defisit-Laporan Operasional
                                                (LO),
                                                aset, kewajiban, ekuitas, dan arus kas Pemerintah Daerah.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step15" class="section-block"><strong class="block-title">D. PENGGUNA
                                                DAN
                                                KEBUTUHAN
                                                INFORMASI</strong>
                                        </div>
                                    </li>

                                    <li>
                                        <div id="step16" class="section-block"><strong class="block-title">Pengguna
                                                Laporan
                                                Keuangan</strong>
                                            </p>
                                            <p>
                                                24. Terdapat beberapa kelompok utama pengguna laporan keuangan
                                                pemerintah
                                                daerah, namun tidak terbatas pada :
                                            </p>
                                            <p>
                                                (a) masyarakat;
                                            </p>
                                            <p>
                                                (b) para wakil rakyat, lembaga pengawas, dan lembaga pemeriksa;
                                            </p>
                                            <p>
                                                (c) pihak yang memberi atau berperan dalam proses donasi, investasi, dan
                                                pinjaman; dan
                                            </p>
                                            <p>
                                                (d) pemerintah yang lebih tinggi (Pemerintah Provinsi dan Pemerintah
                                                Pusat).
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step17" class="section-block"><strong class="block-title">Kebutuhan
                                                Informasi</strong>
                                            </p>
                                            <p>
                                                25. Informasi yang disajikan dalam laporan keuangan bertujuan umum untuk
                                                memenuhi kebutuhan informasi dari semua kelompok pengguna. Dengan
                                                demikian
                                                laporan keuangan pemerintah daerah tidak dirancang untuk memenuhi
                                                kebutuhan
                                                spesifik dari masing-masing kelompok pengguna. Namun demikian, berhubung
                                                laporan keuangan pemerintah daerah berperan sebagai wujud akuntabilitas
                                                pengelolaan keuangan daerah, maka komponen laporan yang disajikan
                                                setidak-tidaknya mencakup jenis laporan dan elemen informasi yang
                                                diharuskan oleh ketentuan peraturan perundang-undangan
                                                (<em>statutory</em>
                                                <em>reports</em>). Selain Dana Alokasi Umum, berhubung pajak merupakan
                                                sumber utama pendapatan pemerintah daerah, maka ketentuan laporan
                                                keuangan
                                                yang memenuhi kebutuhan informasi para pembayar pajak perlu mendapat
                                                perhatian.
                                            </p>
                                            <p>
                                                26. Kebutuhan informasi tentang kegiatan operasional pemerintahan serta
                                                posisi kekayaan dan kewajiban dapat dipenuhi dengan lebih baik dan
                                                memadai
                                                apabila didasarkan pada basis akrual, yakni berdasarkan pengakuan
                                                munculnya
                                                hak dan kewajiban, bukan berdasarkan pada arus kas semata. Namun,
                                                apabila
                                                terdapat ketentuan peraturan perundang-undangan yang mengharuskan
                                                penyajian
                                                suatu laporan keuangan dengan basis kas, maka laporan keuangan dimaksud
                                                wajib disajikan demikian.
                                            </p>
                                            <p>
                                                27. Meskipun memiliki akses terhadap detail informasi yang disajikan
                                                dalam
                                                laporan keuangan, pemerintah daerah wajib memperhatikan informasi yang
                                                disajikan dalam laporan keuangan untuk keperluan perencanaan,
                                                pengendalian
                                                dan pengambilan keputusan. Selanjutnya, pemerintah daerah dapat
                                                menentukan
                                                bentuk dan jenis informasi tambahan untuk kebutuhan sendiri di luar
                                                jenis
                                                informasi yang diatur dalam kerangka konseptual ini maupun
                                                kebijakan-kebijakan akuntansi yang dinyatakan lebih lanjut.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step18" class="section-block"><strong
                                                class="block-title">E.KARAKTERISTIK
                                                KUALITATIF
                                                LAPORAN KEUANGAN</strong>

                                            </p>
                                            <p>
                                                28. Karakteristik kualitatif laporan keuangan adalah ukuran-ukuran
                                                normatif
                                                yang perlu diwujudkan dalam informasi akuntansi sehingga dapat memenuhi
                                                tujuannya.
                                            </p>
                                            <p>
                                                Keempat karakteristik berikut ini merupakan prasyarat normatif yang
                                                diperlukan agar laporan keuangan Pemerintah Daerah dapat memenuhi
                                                kualitas
                                                yang dikehendaki:
                                            </p>
                                            <p>
                                                a) Relevan;
                                            </p>
                                            <p>
                                                b) Andal;
                                            </p>
                                            <p>
                                                c) Dapat dibandingkan; dan
                                            </p>
                                            <p>
                                                d) Dapat dipahami.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step19" class="section-block"><strong
                                                class="block-title">Relevan</strong>
                                            </p>
                                            <p>
                                                29. Laporan keuangan Pemerintah Daerah dikatakan relevan apabila
                                                informasi
                                                yang termuat di dalamnya dapat mempengaruhi keputusan pengguna laporan
                                                keuangan dengan membantunya dalam mengevaluasi peristiwa masa lalu atau
                                                masa kini dan memprediksi masa depan serta menegaskan atau mengoreksi
                                                hasil
                                                evaluasi pengguna laporan di masa lalu. Dengan demikian, informasi
                                                laporan
                                                keuangan yang relevan adalah yang dapat dihubungkan dengan maksud
                                                penggunaannya.
                                            </p>
                                            <p>
                                                30. Informasi yang relevan harus:
                                            </p>
                                            <p>
                                                a. Memiliki manfaat umpan balik (<em>feedback value</em>), artinya bahwa
                                                laporan keuangan Pemerintah Daerah harus memuat informasi yang
                                                memungkinkan
                                                pengguna laporan untuk menegaskan atau mengoreksi ekspektasinya di masa
                                                lalu;
                                            </p>
                                            <p>
                                                b. Memiliki manfaat prediktif (<em>predictive value</em>), artinya bahwa
                                                laporan keuangan harus memuat informasi yang dapat membantu pengguna
                                                laporan untuk memprediksi masa yang akan datang berdasarkan hasil masa
                                                lalu
                                                dan kejadian masa kini;
                                            </p>
                                            <p>
                                                c. Tepat waktu, artinya bahwa laporan keuangan Pemerintah Daerah harus
                                                disajikan tepat waktu sehingga dapat berpengaruh dan berguna untuk
                                                pembuatan keputusan pengguna laporan keuangan; dan
                                            </p>
                                            <p>
                                                d. Lengkap, artinya bahwa penyajian laporan keuangan Pemerintah Daerah
                                                harus memuat informasi yang selengkap mungkin, yaitu mencakup semua
                                                informasi akuntansi yang dapat mempengaruhi pengambilan keputusan dengan
                                                memperhatikan kendala yang ada. Informasi yang melatarbelakangi setiap
                                                butir informasi utama yang termuat dalam laporan keuangan harus
                                                diungkapkan
                                                dengan jelas agar kekeliruan dalam penggunaan informasi tersebut dapat
                                                dicegah.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step20" class="section-block"><strong
                                                class="block-title">Andal</strong>
                                            </p>
                                            <p>
                                                31. Informasi dalam laporan keuangan Pemerintah Daerah harus bebas dari
                                                pengertian yang menyesatkan dan kesalahan material, menyajikan setiap
                                                fakta
                                                secara jujur, serta dapat diverifikasi. Informasi akuntansi yang
                                                relevan,
                                                tetapi jika hakikat atau penyajiannya tidak dapat diandalkan maka
                                                penggunaan informasi tersebut secara potensial dapat menyesatkan.
                                                Informasi
                                                yang andal harus memenuhi karakteristik:
                                            </p>
                                            <p>
                                                a. Penyajiannya jujur, artinya bahwa laporan keuangan Pemerintah Daerah
                                                harus memuat informasi yang menggambarkan dengan jujur transaksi serta
                                                peristiwa lainnya yang seharusnya disajikan atau yang secara wajar dapat
                                                diharapkan untuk disajikan;
                                            </p>
                                            <p>
                                                b. Dapat diverifikasi (<em>verifiability</em>), artinya bahwa laporan
                                                keuangan Pemerintah Daerah harus memuat informasi yang dapat diuji, dan
                                                apabila pengujian dilakukan lebih dari sekali oleh pihak yang berbeda,
                                                hasilnya harus tetap menunjukkan simpulan yang tidak jauh berbeda;
                                            </p>
                                            <p>
                                                c. Netralitas, artinya bahwa laporan keuangan Pemerintah Daerah harus
                                                memuat informasi yang diarahkan untuk memenuhi kebutuhan umum dan tidak
                                                berpihak pada kebutuhan pihak tertentu.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step21" class="section-block"><strong class="block-title">Dapat
                                                Dibandingkan</strong>
                                            </p>
                                            <p>
                                                32. Informasi yang termuat dalam laporan keuangan Pemerintah Daerah akan
                                                lebih berguna jika dapat dibandingkan dengan laporan keuangan periode
                                                sebelumnya atau laporan keuangan pemerintah daerah lain pada umumnya.
                                                Perbandingan dapat dilakukan secara internal dan eksternal. Perbandingan
                                                secara internal dapat dilakukan bila pemerintah daerah menerapkan
                                                kebijakan
                                                akuntansi yang sama dari tahun ke tahun. Perbandingan secara eksternal
                                                dapat dilakukan bila pemerintah daerah yang diperbandingkan menerapkan
                                                kebijakan akuntansi yang sama. Apabila Pemerintah Daerah akan menerapkan
                                                kebijakan akuntansi yang lebih baik daripada kebijakan akuntansi yang
                                                sekarang diterapkan, perubahan kebijakan akuntansi harus diungkapkan
                                                pada
                                                periode terjadinya perubahan tersebut.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
                                    <li>
                                        <div id="step22" class="section-block"><strong class="block-title">Dapat
                                                Dipahami</strong>
                                            </p>
                                            <p>
                                                33. Informasi yang disajikan dalam laporan keuangan harus dapat dipahami
                                                oleh pengguna laporan keuangan dan dinyatakan dalam bentuk serta istilah
                                                yang disesuaikan dengan batas pemahaman para pengguna laporan. Untuk
                                                itu,
                                                pengguna laporan diasumsikan memiliki pengetahuan yang memadai atas
                                                kegiatan dan lingkungan operasi Pemerintah Daerah, serta adanya kemauan
                                                pengguna laporan untuk mempelajari informasi yang dimaksud.
                                            </p>
                                            <p>
                                        </div>
                                    </li>
